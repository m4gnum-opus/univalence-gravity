    a("  (r : D200Region) →")
    a("  S-cut d200BdyView r ≡ L-min d200BulkView r")
    a("d200-pointwise r = d200-pointwise-rep (classify200 r)")
    a("")

    # ── Function path ───────────────────────────────────────────────
    a("S∂D200 : D200Region → ℚ≥0")
    a("S∂D200 = S-cut d200BdyView")
    a("")
    a("LBD200 : D200Region → ℚ≥0")
    a("LBD200 = L-min d200BulkView")
    a("")
    a("d200-obs-path : S∂D200 ≡ LBD200")
    a("d200-obs-path = funExt d200-pointwise")
    a("")

