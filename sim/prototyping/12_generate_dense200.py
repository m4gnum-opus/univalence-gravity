def gen_area_law(regions: list[RegionInfo]) -> str:
    """Generate  Boundary/Dense200AreaLaw.agda ."""
    L: list[str] = []
    a = L.append

    a("{-# OPTIONS --cubical --safe --guardedness #-}")
    a("")
    a("module Boundary.Dense200AreaLaw where")
    a("")
    a("open import Cubical.Foundations.Prelude")
    a("open import Cubical.Data.Nat using (ℕ ; _+_)")
    a("open import Util.Scalars")
    a("open import Common.Dense200Spec")
    a("open import Boundary.Dense200Cut")
    a("open import Bridge.Dense200Obs using (d200BdyView)")
    a("")

    # ── regionArea ──────────────────────────────────────────────────
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  regionArea — Boundary surface area of each region")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--")
    a("--  area(A) = 6k − 2 · |internal faces within A|")
    a("--")
    a(GENERATED_BANNER)
    a("")
    a("regionArea : D200Region → ℚ≥0")

    for r in regions:
        rn = region_agda_name(r)
        n = len(r.cells)
        a(f"regionArea {rn} = {r.boundary_area}"
          f"    -- {n} cells")

    a("")

    # ── area-law (abstract) ─────────────────────────────────────────
    a("-- ═══════════════════════════════════════════════════════════")
    a(f"--  area-law — S_cut(A) ≤ area(A)  ({len(regions)} cases)")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--")
    a("--  Wrapped in  abstract  so downstream modules never")
    a("--  re-unfold the proof.  Since  _≤ℚ_  is propositional,")
    a("--  the abstract barrier does not damage the Univalence bridge.")
    a("--")
    a(GENERATED_BANNER)
    a("")
    a("abstract")
    a("  area-law : (r : D200Region)"
      " → S-cut d200BdyView r ≤ℚ regionArea r")

    prev_key = (-1, -1)
    for r in regions:
        key = (r.min_cut, r.slack)
        if key != prev_key:
            a(f"  -- S={r.min_cut}, area={r.boundary_area},"
              f" slack={r.slack}")
            prev_key = key
        rn = region_agda_name(r)
        a(f"  area-law {rn} = {r.slack} , refl")

    a("")
    return "\n".join(L) + "\n"


