class RegionInfo(NamedTuple):
    idx: int
    cells: frozenset[int]
    n_bdy_faces: int         # number of boundary pseudo-nodes in region
    min_cut: int             # min-cut value (from max-flow)
    boundary_area: int       # 6|A| − 2·|internal faces within A|
    slack: int               # boundary_area − min_cut  (must be ≥ 0)


def compute_boundary_area(
    cells: frozenset[int],
    internal_faces: list[tuple[int, int]],
) -> int:
    """
    Boundary surface area of a cell-aligned region A.

        area(A) = 6·|A| − 2·|{shared faces between two cells in A}|

    Each cube has 6 faces.  Each internal face shared between two
    A-cells is counted by both cells (contributing 2 to the 6|A|
    total) but does not exit A — so we subtract 2 per shared face.
    """
    n_cells = len(cells)
    internal_within = sum(
        1 for c1, c2 in internal_faces
        if c1 in cells and c2 in cells
    )
    return 6 * n_cells - 2 * internal_within


def compute_regions(patch: dict) -> list[RegionInfo]:
    """
    Enumerate all cell-aligned boundary regions (connected subsets
    of boundary cells up to MAX_REGION_CELLS cells), compute their
    min-cut values and boundary surface areas.
    """
    flow_G, bnode_ids, bnode_to_cell = multi.build_flow_graph(patch)
    internal_faces = patch["internal_faces"]

    regions_raw = multi.enumerate_cell_aligned_regions(
        patch, bnode_to_cell, bnode_ids,
        max_region_cells=MAX_REGION_CELLS,
    )

    regions: list[RegionInfo] = []
    for label, idx_set in regions_raw:
        cells_str = label.strip("{}").split(",")
        cells = frozenset(
            int(s.strip().lstrip("c")) for s in cells_str if s.strip()
        )
        s = multi.compute_min_cut(flow_G, bnode_ids, idx_set)
        area = compute_boundary_area(cells, internal_faces)
        slack = area - s
        regions.append(RegionInfo(
            idx=len(regions),
            cells=cells,
            n_bdy_faces=len(idx_set),
            min_cut=s,
            boundary_area=area,
            slack=slack,
        ))

    return regions


# ════════════════════════════════════════════════════════════════════
#  3.  Orbit Reduction (by min-cut value)
