def gen_spec(
    regions: list[RegionInfo],
    orbits: list[OrbitRep],
) -> str:
    """Generate  Common/Dense200Spec.agda ."""
    L: list[str] = []
    a = L.append

    a("{-# OPTIONS --cubical --safe --guardedness #-}")
    a("")
    a("module Common.Dense200Spec where")
    a("")
    a("open import Cubical.Foundations.Prelude")
    a("open import Util.Scalars")
    a("")

    # ── D200Region ──────────────────────────────────────────────────
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  D200Region — Cell-aligned boundary regions of the Dense-200")
    a("--  patch of the {4,3,5} hyperbolic honeycomb")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--")
    a(f"--  The Dense-200 patch is grown by greedy max-connectivity")
    a(f"--  from the central cube, producing {MAX_CELLS} cells.")
    a(f"--  Regions are connected subsets of boundary cells (up to")
    a(f"--  {MAX_REGION_CELLS} cells), giving {len(regions)} regions.")
    a("--")
    a("--  This is the third resolution level in the convergence")
    a("--  certificate, extending the tower:")
    a("--    Dense-50 (139 regions) → Dense-100 (717 regions)")
    a(f"--    → Dense-200 ({len(regions)} regions)")
    a("--")
    a("--  The orbit reduction strategy classifies regions by")
    a(f"--  min-cut value into {len(orbits)} orbit representatives.")
    a("--")
    a(GENERATED_BANNER)
    a("")
    a("data D200Region : Type₀ where")

    sizes: dict[int, list[RegionInfo]] = defaultdict(list)
    for r in regions:
        sizes[len(r.cells)].append(r)

    for size in sorted(sizes.keys()):
        rs = sizes[size]
        names = [region_agda_name(r) for r in rs]
        a(f"  -- size {size}  ({len(rs)} regions)")
        for i in range(0, len(names), 10):
            batch = names[i:i + 10]
            a(f"  {' '.join(batch)} : D200Region")
    a("")

    # ── D200OrbitRep ────────────────────────────────────────────────
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  D200OrbitRep — Orbit representatives by min-cut value")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--")
    a("--  Orbit decomposition:")
    for orb in orbits:
        a(f"--    {orb.name}  (S = {orb.min_cut}):  {orb.count} regions")
    a("--")
    a(GENERATED_BANNER)
    a("")
    a("data D200OrbitRep : Type₀ where")
    orb_names = [orb.name for orb in orbits]
    a(f"  {' '.join(orb_names)} : D200OrbitRep")
    a("")

    # ── classify200 ─────────────────────────────────────────────────
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  classify200 — Map each region to its orbit representative")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--")
    a(f"--  {len(regions)}-clause pattern match.  Each clause maps")
    a("--  a constructor to an orbit representative.  This function")
    a("--  is traversed only during concrete evaluation, never")
    a("--  during proof normalization.")
    a("--")
    a(GENERATED_BANNER)
    a("")
    a("classify200 : D200Region → D200OrbitRep")

    for r in regions:
        rn = region_agda_name(r)
        orb = orbit_for_region(r, orbits)
        a(f"classify200 {rn} = {orb.name}")

    a("")
    return "\n".join(L) + "\n"


