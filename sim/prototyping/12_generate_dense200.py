class OrbitRep(NamedTuple):
    """An orbit representative, identified by its min-cut value."""
    name: str       # Agda constructor name: mc1, mc2, ...
    min_cut: int    # The shared min-cut value for all regions in this orbit
    count: int      # Number of regions in this orbit


def compute_orbits(regions: list[RegionInfo]) -> list[OrbitRep]:
    """Group regions by min-cut value into orbit representatives."""
    cut_counts = Counter(r.min_cut for r in regions)
    orbits: list[OrbitRep] = []
    for mc_val in sorted(cut_counts.keys()):
        orbits.append(OrbitRep(
            name=f"mc{mc_val}",
            min_cut=mc_val,
            count=cut_counts[mc_val],
        ))
    return orbits


def orbit_for_region(r: RegionInfo, orbits: list[OrbitRep]) -> OrbitRep:
    """Find the orbit representative for a region."""
    for orb in orbits:
        if orb.min_cut == r.min_cut:
            return orb
    raise ValueError(f"No orbit for min-cut {r.min_cut}")


# ════════════════════════════════════════════════════════════════════
#  4.  Edge Curvature Classes
# ════════════════════════════════════════════════════════════════════

class EdgeClassInfo(NamedTuple):
    name: str
    valence_in_patch: int
    full_valence: int
    count: int
    kappa_twentieths: int


def compute_edge_classes(data: dict, patch: dict) -> list[EdgeClassInfo]:
    """
    Classify the 12 edges of the central cube by their in-patch
    valence using the deduplicated orbit-center approach from 07.
    """
    face_crossings = data["face_crossings"]
    adj_pairs = data["adj_pairs"]
    cell_center = data["cell_center"]
    p = cell_center

    patch_centers = {multi._vec_key(mat @ p)
                     for mat in patch["cells"].values()}

    valences: list[int] = []
    for fi, fj in adj_pairs:
        F_a, F_b = face_crossings[fi], face_crossings[fj]
        dih_elems = multi.enumerate_group([F_a, F_b])
        orbit_centers = {multi._vec_key(g @ p) for g in dih_elems}
        in_patch = len(orbit_centers & patch_centers)
        valences.append(in_patch)

    val_counts = Counter(valences)
    classes: list[EdgeClassInfo] = []
    for val in sorted(val_counts.keys()):
        k20 = 20 - 5 * val
        classes.append(EdgeClassInfo(
            name=f"ev{val}",
            valence_in_patch=val,
            full_valence=5,
            count=val_counts[val],
            kappa_twentieths=k20,
        ))
    return classes


# ════════════════════════════════════════════════════════════════════
