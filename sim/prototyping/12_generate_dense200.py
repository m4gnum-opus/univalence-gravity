def print_summary(
    regions: list[RegionInfo],
    orbits: list[OrbitRep],
    edge_classes: list[EdgeClassInfo],
    patch: dict,
) -> None:
    print(f"\n  ── Patch Topology ──")
    print(f"  Cells:            {len(patch['cells'])}")
    print(f"  Internal faces:   {len(patch['internal_faces'])}")
    print(f"  Boundary faces:   {patch['n_boundary_faces']}")

    density = 2 * len(patch["internal_faces"]) / max(len(patch["cells"]), 1)
    print(f"  Density (2·IntF/Cells): {density:.2f}")

    cut_dist = Counter(r.min_cut for r in regions)
    size_dist = Counter(len(r.cells) for r in regions)
    slack_dist = Counter(r.slack for r in regions)

    print(f"\n  ── Region Statistics ({len(regions)} total) ──")
    print(f"  By size:")
    for sz in sorted(size_dist):
        rs = [r for r in regions if len(r.cells) == sz]
        min_mc = min(r.min_cut for r in rs)
        max_mc = max(r.min_cut for r in rs)
        min_area = min(r.boundary_area for r in rs)
        max_area = max(r.boundary_area for r in rs)
        print(f"    {sz}-cell:  {size_dist[sz]:>5d} regions, "
              f"area {min_area}–{max_area}, min-cut {min_mc}–{max_mc}")

    print(f"  By min-cut value:")
    for val in sorted(cut_dist):
        print(f"    S = {val:>2d}:  {cut_dist[val]:>5d} regions")

    min_s = min(r.min_cut for r in regions)
    max_s = max(r.min_cut for r in regions)
    print(f"  Min-cut range:  {min_s} – {max_s}")

    print(f"\n  ── Orbit Reduction ──")
    print(f"  Distinct min-cut values:  {len(orbits)}")
    print(f"  Orbit decomposition:")
    for orb in orbits:
        print(f"    {orb.name}  (S = {orb.min_cut}):  {orb.count} regions")
    reduction = len(regions) / max(len(orbits), 1)
    print(f"  Reduction factor:  {len(regions)} → {len(orbits)}"
          f"  ({reduction:.0f}× fewer proof cases)")

    print(f"\n  ── Area-Law Statistics ──")
    min_slack = min(r.slack for r in regions)
    max_slack = max(r.slack for r in regions)
    avg_slack = sum(r.slack for r in regions) / len(regions)
    tight = sum(1 for r in regions if r.slack == 0)
    violations = sum(1 for r in regions if r.slack < 0)
    print(f"  Slack range:    {min_slack} – {max_slack}  (mean {avg_slack:.1f})")
    print(f"  Tight (S=area): {tight} / {len(regions)}")
    print(f"  Violations:     {violations}")
    print(f"  Slack distribution:")
    for k in sorted(slack_dist):
        print(f"    k = {k:>2d}:  {slack_dist[k]:>5d} regions")

    print(f"\n  ── Resolution Tower Comparison ──")
    print(f"    Dense-50:   139 regions,  max S = 7")
    print(f"    Dense-100:  717 regions,  max S = 8")
    print(f"    Dense-200:  {len(regions)} regions,  max S = {max_s}")

    print(f"\n  ── Edge Curvature (12 central-cube edges) ──")
    for ec in edge_classes:
        k = ec.kappa_twentieths
        tag = " ← hyperbolic" if k < 0 else (" (flat)" if k == 0 else "")
        print(f"    Valence {ec.valence_in_patch} (×{ec.count}): "
              f"κ₂₀ = {k}{tag}")
    total_k = sum(ec.count * ec.kappa_twentieths for ec in edge_classes)
    print(f"  Total:  Σ κ₂₀ = {total_k}")


def print_monotonicity(regions: list[RegionInfo]) -> None:
    """Analyze and print the monotonicity witness for the tower."""
    max_s = max(r.min_cut for r in regions)
    k = max_s - DENSE100_MAX_MINCUT

    print(f"\n  ════════════════════════════════════════════════════════")
    print(f"  MONOTONICITY WITNESS (Dense-100 → Dense-200)")
    print(f"  ════════════════════════════════════════════════════════")
    print(f"  Dense-100 max min-cut:  {DENSE100_MAX_MINCUT}")
    print(f"  Dense-200 max min-cut:  {max_s}")

    if k > 0:
        print(f"\n  ✓  Spectrum GROWS:  {DENSE100_MAX_MINCUT} < {max_s}")
        print(f"     Agda witness:  spectrum-grows-200 : "
              f"{DENSE100_MAX_MINCUT} ≤ℚ {max_s}")
        print(f"     spectrum-grows-200 = {k} , refl")
        print(f"     (because {k} + {DENSE100_MAX_MINCUT} = {max_s} "
              f"judgmentally)")
    elif k == 0:
        print(f"\n  ≡  Spectrum FLAT:  {DENSE100_MAX_MINCUT} = {max_s}")
        print(f"     Agda witness:  spectrum-grows-200 : "
              f"{DENSE100_MAX_MINCUT} ≤ℚ {max_s}")
        print(f"     spectrum-grows-200 = 0 , refl")
        print(f"     (non-strict monotonicity; max did not increase)")
    else:
        print(f"\n  ✗  Spectrum SHRINKS:  {DENSE100_MAX_MINCUT} > {max_s}")
        print(f"     This is unexpected and may indicate a patch issue.")

    print(f"\n  Resolution tower monotonicity table:")
    print(f"    | Transition             | Max_lo | Max_hi | Witness      |")
    print(f"    |──────────────────────--|────────|────────|──────────────|")
    print(f"    | Dense-50 → Dense-100   |      7 |      8 | (1 , refl)  |")
    if k >= 0:
        print(f"    | Dense-100 → Dense-200  |      8 |"
              f"     {max_s:>2d} | ({k} , refl)  |")
    else:
        print(f"    | Dense-100 → Dense-200  |      8 |"
              f"     {max_s:>2d} | VIOLATION    |")


def print_gate_check(
    regions: list[RegionInfo],
    orbits: list[OrbitRep],
    edge_classes: list[EdgeClassInfo],
    patch: dict,
) -> bool:
    n_bdy = patch["n_boundary_faces"]
    n_regs = len(regions)
    n_orbs = len(orbits)
    n_full = sum(ec.count for ec in edge_classes
                 if ec.valence_in_patch >= 5)
    max_cut = max(r.min_cut for r in regions) if regions else 0
    area_ok = all(r.slack >= 0 for r in regions)

    print(f"\n  ═══════════════════════════════════════════════════════")
    print(f"  GATE CHECK (Dense-200 — Phase G.1)")
    print(f"  ═══════════════════════════════════════════════════════")

    gate_pass = True
    checks = [
        ("Region count (orbit ≤ 200)",
         n_orbs <= 200, f"{n_regs} raw / {n_orbs} orbits"),
        ("≥1 full-valence edge in patch",
         n_full > 0, n_full),
        ("Max min-cut ≥ 8 (at least matches Dense-100)",
         max_cut >= DENSE100_MAX_MINCUT, max_cut),
        ("Area law holds (S ≤ area for all regions)",
         area_ok, f"{'✓' if area_ok else '✗'}"),
        ("Orbit reduction effective (ratio > 5×)",
         n_regs / max(n_orbs, 1) > 5,
         f"{n_regs / max(n_orbs, 1):.0f}×"),
    ]
    for desc, ok, val in checks:
        status = "✓ PASS" if ok else "✗ FAIL"
        if not ok:
            gate_pass = False
        print(f"    {status}  {desc}  (actual: {val})")

    if gate_pass:
        print(f"\n  ▶  ALL GATES PASS — Dense-200 extends the "
              f"convergence certificate!")
    else:
        print(f"\n  ▶  GATE(S) BLOCKED")

    return gate_pass


