def region_agda_name(r: RegionInfo) -> str:
    return f"d200r{r.idx}"


def cells_comment(r: RegionInfo) -> str:
    cs = sorted(r.cells)
    if len(cs) <= 6:
        return "{" + ",".join(f"c{c}" for c in cs) + "}"
    return "{" + ",".join(f"c{c}" for c in cs[:5]) + ",..." + "}"


