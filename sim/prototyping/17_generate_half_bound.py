#  7.  Main
# ════════════════════════════════════════════════════════════════════

def main() -> None:
    parser = argparse.ArgumentParser(
        description="Generate HalfBoundWitness Agda modules for "
                    "Dense-100 and Dense-200 patches (§15.11 Phase A)")
    parser.add_argument("--output-dir", type=Path, default=DEFAULT_SRC_DIR,
                        help="Root source directory "
                             "(default: ../../src relative to script)")
    parser.add_argument("--dry-run", action="store_true",
                        help="Compute and verify without writing files")
    args = parser.parse_args()

    print("╔═══════════════════════════════════════════════════════════╗")
    print("║  §15.11 Phase A:  Per-Instance HalfBound Witnesses       ║")
    print("║  2·S(A) ≤ area(A)  — Dense-100 and Dense-200 patches    ║")
    print("╚═══════════════════════════════════════════════════════════╝")

    src_dir = args.output_dir.resolve()
    targets = [100, 200]
    all_ok = True

    for n_cells in targets:
        # ── Build patch ─────────────────────────────────────────────
        print(f"\n  Building Dense-{n_cells} patch ...", flush=True)
        t0 = time.time()
        patch = build_dense_patch(n_cells)
        nc = len(patch["cells"])
        nf = len(patch["internal_faces"])
        nb = patch["n_boundary_faces"]
        print(f"  ✓  {nc} cells, {nf} internal faces, {nb} boundary faces."
              f"  ({time.time() - t0:.1f}s)")

        # ── Compute region data ─────────────────────────────────────
        print(f"  Computing half-bound data for all regions "
              f"(max_rc={MAX_REGION_CELLS}) ...", flush=True)
        t0 = time.time()
        data = compute_half_bound_data(patch)
        print(f"  ✓  {len(data)} regions computed.  ({time.time() - t0:.1f}s)")

        if not data:
            print(f"  ✗  No regions found for Dense-{n_cells}!")
            all_ok = False
            continue

        # ── Verify and find achiever ────────────────────────────────
        ok, achiever = verify_and_summarize(n_cells, data)
        if not ok or achiever is None:
            all_ok = False
            print(f"  ✗  Cannot generate module for Dense-{n_cells}.")
            continue

        # ── Generate Agda module ────────────────────────────────────
        content = generate_half_bound_module(n_cells, data, achiever)
        out_path = src_dir / "Boundary" / f"Dense{n_cells}HalfBound.agda"
        print(f"\n  Output: {out_path}")
        write_file(out_path, content, args.dry_run)

    # ── Final report ────────────────────────────────────────────────
    print(f"\n{'═' * 60}")
    if all_ok:
        action = "would generate" if args.dry_run else "generated"
        print(f"  ✓  Successfully {action} {len(targets)} modules.")
    else:
        print(f"  ✗  Some modules could not be generated.")

    print(f"""
  Each generated module provides a  HalfBoundWitness pd  containing:
    • area       :  regionArea  (imported from DenseNAreaLaw)
    • half-bound :  abstract proof,  (k , refl) per region
    • tight      :  concrete achiever where 2·S = area

  The strengthened bound  2·S ≤ area  replaces the weaker  S ≤ area
  from the AreaLaw modules.  Together with the generic  from-two-cuts
  lemma in  Bridge/HalfBound.agda , this establishes:

    1/(4G) = 1/2  in bond-dimension-1 units

  as an exact discrete constant — the discrete Newton's constant.

  This eliminates the constructive-reals wall for the entropy-area
  relationship: no convergence argument needed, just refl on closed
  ℕ terms at every resolution level.

  Next steps:
    1. Load generated modules in Agda to verify they type-check.
    2. Extend  Bridge/SchematicTower.agda  to carry HalfBoundWitness
       at each tower level.
    3. Update  Bridge/Dense100Thermodynamics.agda  to use the
       strengthened bound.
""")


if __name__ == "__main__":
    main()