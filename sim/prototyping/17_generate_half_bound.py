def region_name(prefix: str, d: HalfBoundDatum) -> str:
    """Agda constructor name for a region: d100r0, d200r0, ..."""
    return f"{prefix}r{d.idx}"


# ════════════════════════════════════════════════════════════════════
#  4.  Agda Module Generator
# ════════════════════════════════════════════════════════════════════

def generate_half_bound_module(
    n_cells: int,
    data: list[HalfBoundDatum],
    achiever: HalfBoundDatum,
) -> str:
    """
    Generate a  Boundary/DenseNHalfBound.agda  module.

    The module instantiates  HalfBoundWitness pd  where  pd  is the
    PatchData for the Dense-N patch (from GenericValidation), using
    regionArea from the corresponding AreaLaw module.
    """
    # Determine naming prefixes based on cell count
    N = n_cells
    prefix = f"d{N}"       # d100, d200
    mod_N = str(N)          # "100", "200"

    L: list[str] = []
    a = L.append

    # ── Header ──────────────────────────────────────────────────────
    a("{-# OPTIONS --cubical --safe --guardedness #-}")
    a("")
    a(f"module Boundary.Dense{mod_N}HalfBound where")
    a("")
    a("open import Cubical.Foundations.Prelude")
    a("open import Cubical.Data.Nat using (ℕ ; _+_)")
    a("open import Cubical.Data.Sigma using (Σ-syntax)")
    a("")
    a("open import Util.Scalars")
    a(f"open import Common.Dense{mod_N}Spec")
    a(f"open import Boundary.Dense{mod_N}AreaLaw using (regionArea)")
    a("open import Bridge.GenericBridge using (PatchData ; orbit-to-patch)")
    a(f"open import Bridge.GenericValidation using (d{mod_N}OrbitPatch)")
    a("open import Bridge.HalfBound using (HalfBoundWitness)")
    a("")
    a(GENERATED_BANNER)
    a("")

    # ── PatchData and S∂ shorthand ──────────────────────────────────
    a("-- ═══════════════════════════════════════════════════════════")
    a(f"--  PatchData for the Dense-{mod_N} patch")
    a("-- ═══════════════════════════════════════════════════════════")
    a("")
    a("private")
    a("  pd : PatchData")
    a(f"  pd = orbit-to-patch d{mod_N}OrbitPatch")
    a("")
    a(f"  S∂ : D{mod_N}Region → ℚ≥0")
    a("  S∂ = PatchData.S∂ pd")
    a("")

    # ── Half-bound proof (abstract) ─────────────────────────────────
    a("-- ═══════════════════════════════════════════════════════════")
    a(f"--  half-bound-proof — 2·S(A) ≤ area(A)  ({len(data)} cases)")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--")
    a("--  The discrete Bekenstein–Hawking bound:  S(A) ≤ area(A)/2.")
    a("--  Stated as  (S∂ r + S∂ r) ≤ℚ regionArea r  to avoid")
    a("--  division in ℕ.")
    a("--")
    a("--  Each witness  (k , refl)  type-checks because")
    a("--  k + (S∂ r + S∂ r) reduces judgmentally to regionArea r")
    a("--  via ℕ addition on closed numerals.")
    a("--")
    a("--  Wrapped in  abstract  so downstream modules never")
    a("--  re-unfold the proof.  Since  _≤ℚ_  is propositional,")
    a("--  the abstract barrier does not damage the Univalence bridge.")
    a("--")
    a(GENERATED_BANNER)
    a("")
    a("abstract")
    a(f"  half-bound-proof : (r : D{mod_N}Region)"
      f" → (S∂ r +ℚ S∂ r) ≤ℚ regionArea r")

    # Group by (min_cut, half_slack) for readability
    prev_key = (-1, -1)
    for d in data:
        key = (d.min_cut, d.half_slack)
        if key != prev_key:
            a(f"  -- S={d.min_cut}, area={d.boundary_area},"
              f" 2·S={2*d.min_cut}, slack={d.half_slack}")
            prev_key = key
        rn = region_name(prefix, d)
        a(f"  half-bound-proof {rn} = {d.half_slack} , refl")

    a("")

    # ── Tight witness ───────────────────────────────────────────────
    ach_rn = region_name(prefix, achiever)
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  tight-witness — A region achieving 2·S = area")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--")
    a(f"--  {ach_rn}: S={achiever.min_cut}, area={achiever.boundary_area},"
      f" 2·{achiever.min_cut}={2*achiever.min_cut}={achiever.boundary_area}")
    a(f"--  This is a {len(achiever.cells)}-cell region with"
      f" n_cross = n_bdy = {achiever.boundary_area // 2}.")
    a("--")
    a("")
    a(f"tight-witness :"
      f" Σ[ r ∈ D{mod_N}Region ] (S∂ r +ℚ S∂ r ≡ regionArea r)")
    a(f"tight-witness = {ach_rn} , refl")
    a("")

    # ── HalfBoundWitness instantiation ──────────────────────────────
    a("-- ═══════════════════════════════════════════════════════════")
    a(f"--  dense{mod_N}HalfBound — The complete HalfBoundWitness")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--")
    a("--  Packages the area function, the abstract half-bound proof,")
    a("--  and the tight achiever witness into a single record.")
    a("--  This is the per-instance deliverable from §15.11 Phase A.")
    a("--")
    a("")
    a(f"dense{mod_N}HalfBound : HalfBoundWitness pd")
    a(f"dense{mod_N}HalfBound .HalfBoundWitness.area = regionArea")
    a(f"dense{mod_N}HalfBound .HalfBoundWitness.half-bound"
      f" = half-bound-proof")
    a(f"dense{mod_N}HalfBound .HalfBoundWitness.tight = tight-witness")
    a("")

    return "\n".join(L) + "\n"


# ════════════════════════════════════════════════════════════════════
#  5.  Verification and Summary
# ════════════════════════════════════════════════════════════════════

def verify_and_summarize(
    n_cells: int,
    data: list[HalfBoundDatum],
) -> tuple[bool, HalfBoundDatum | None]:
    """
    Verify the half-bound holds for all regions and find an achiever.

    Returns (all_ok, achiever_or_None).
    """
    violations = [d for d in data if d.half_slack < 0]
    achievers = [d for d in data if d.half_slack == 0]

    print(f"\n  ── Dense-{n_cells} Half-Bound Statistics"
          f" ({len(data)} regions) ──")

    slack_dist = Counter(d.half_slack for d in data)
    print(f"  Half-slack distribution (area − 2·S):")
    for k in sorted(slack_dist):
        print(f"    k = {k:>2d}:  {slack_dist[k]:>5d} regions")

    min_slack = min(d.half_slack for d in data)
    max_slack = max(d.half_slack for d in data)
    mean_slack = sum(d.half_slack for d in data) / len(data)
    print(f"  Slack range:  {min_slack} – {max_slack}  "
          f"(mean {mean_slack:.1f})")

    if violations:
        print(f"\n  ✗  {len(violations)} VIOLATIONS of 2·S ≤ area!")
        for v in violations[:5]:
            print(f"      idx={v.idx}: S={v.min_cut}, area={v.boundary_area},"
                  f" 2·S={2*v.min_cut}, slack={v.half_slack}")
        return False, None

    print(f"  ✓  Half-bound 2·S ≤ area verified for all"
          f" {len(data)} regions.")

    if achievers:
        ach = achievers[0]
        n_ach = len(achievers)
        size_dist = Counter(len(a.cells) for a in achievers)
        print(f"  Achievers (2·S = area): {n_ach} regions")
        for k, cnt in sorted(size_dist.items()):
            # Find an example at this size
            ex = next(a for a in achievers if len(a.cells) == k)
            print(f"    k={k} cells: {cnt} regions  "
                  f"(e.g. S={ex.min_cut}, area={ex.boundary_area})")
        return True, ach
    else:
        print(f"  ✗  No achiever found (2·S < area for all regions).")
        print(f"     Cannot populate the 'tight' field.")
        return False, None


# ════════════════════════════════════════════════════════════════════
#  6.  File Writing
# ════════════════════════════════════════════════════════════════════

def write_file(path: Path, content: str, dry_run: bool) -> None:
    """Write content to path, creating parent directories as needed."""
    lines = content.count("\n")
    if dry_run:
        print(f"  [dry-run]  {path}  ({lines} lines)")
    else:
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content, encoding="utf-8")
        print(f"  ✓  {path}  ({lines} lines)")


