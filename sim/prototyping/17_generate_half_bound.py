class HalfBoundDatum(NamedTuple):
    """Per-region data for the half-bound witness."""
    idx: int
    cells: frozenset[int]
    min_cut: int         # S(A)
    boundary_area: int   # area(A) = 6k − 2·|internal faces within A|
    half_slack: int      # area(A) − 2·S(A)   (must be ≥ 0)


# ════════════════════════════════════════════════════════════════════
#  2.  Patch Building and Region Computation
# ════════════════════════════════════════════════════════════════════

MAX_REGION_CELLS = 5


def build_dense_patch(max_cells: int) -> dict:
    """Build a Dense-N patch using the {4,3,5} greedy strategy."""
    G = multi.build_gram_matrix()
    refls = multi.build_reflections(G)
    multi.validate_reflections(G, refls)
    cell_center = multi.find_cell_center(G)
    stab = multi.enumerate_group(refls[:3], expected=48)
    face_crossings = multi.find_face_crossings(refls, stab, G)
    patch = multi.build_patch_dense(
        face_crossings, cell_center, max_cells,
        name=f"Dense-{max_cells}")
    return patch


def compute_boundary_area(
    cells: frozenset[int],
    internal_faces: list[tuple[int, int]],
) -> int:
    """area(A) = 6·|A| − 2·|{shared faces between two cells in A}|"""
    internal_within = sum(
        1 for c1, c2 in internal_faces
        if c1 in cells and c2 in cells
    )
    return 6 * len(cells) - 2 * internal_within


def compute_half_bound_data(patch: dict) -> list[HalfBoundDatum]:
    """
    Enumerate all cell-aligned boundary regions, compute min-cuts
    and boundary areas, and derive half-bound slack values.
    """
    flow_G, bnode_ids, bnode_to_cell = multi.build_flow_graph(patch)
    internal_faces = patch["internal_faces"]

    regions_raw = multi.enumerate_cell_aligned_regions(
        patch, bnode_to_cell, bnode_ids,
        max_region_cells=MAX_REGION_CELLS,
    )

    data: list[HalfBoundDatum] = []
    for label, idx_set in regions_raw:
        cells_str = label.strip("{}").split(",")
        cells = frozenset(
            int(s.strip().lstrip("c")) for s in cells_str if s.strip()
        )
        s = multi.compute_min_cut(flow_G, bnode_ids, idx_set)
        area = compute_boundary_area(cells, internal_faces)
        half_slack = area - 2 * s
        data.append(HalfBoundDatum(
            idx=len(data),
            cells=cells,
            min_cut=s,
            boundary_area=area,
            half_slack=half_slack,
        ))

    return data


# ════════════════════════════════════════════════════════════════════
#  3.  Agda Naming Helpers
