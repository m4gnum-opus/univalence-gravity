def gen_curvature(edge_classes: list[EdgeClassInfo]) -> str:
    """Generate Bulk/Honeycomb3DCurvature.agda."""
    L: list[str] = []
    a = L.append

    a("{-# OPTIONS --cubical --safe --guardedness #-}")
    a("")
    a("module Bulk.Honeycomb3DCurvature where")
    a("")
    a("open import Cubical.Foundations.Prelude")
    a("open import Cubical.Data.Nat  using (ℕ ; zero ; suc ; _+_ ; _·_)")
    a("open import Cubical.Data.Int")
    a("  using (ℤ ; pos ; negsuc)")
    a("  renaming (_+_ to _+ℤ_)")
    a("")
    a("open import Util.Rationals")
    a("")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  3D Edge Curvature for the {4,3,5} Honeycomb Patch")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--")
    a("--  In 3D, curvature lives on EDGES (not vertices).")
    a("--  At an interior edge e with valence v (cubes meeting):")
    a("--    κ(e) = 2π − v · (π/2)")
    a("--")
    a("--  For {4,3,5}: full valence = 5, so κ = −π/2 (hyperbolic).")
    a("--  In the flat cubic tiling {4,3,4}: valence = 4, κ = 0.")
    a("--")
    a("--  Combinatorial encoding:  ℚ₁₀ with denominator 20.")
    a("--  κ₂₀(e) = 20 · (1 − v/4) = 20 − 5v   (in twentieths)")
    a("--    v=5 → κ₂₀ = −5   (hyperbolic)")
    a("--    v=4 → κ₂₀ =  0   (flat)")
    a("--    v=3 → κ₂₀ = +5   (boundary)")
    a("--    v=2 → κ₂₀ = +10  (boundary)")
    a("--")
    a(GENERATED_BANNER)
    a("")

    # Edge class data type
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  H3EdgeClass — Edge classes by in-patch valence")
    a("-- ═══════════════════════════════════════════════════════════")
    a("")
    a("data H3EdgeClass : Type₀ where")
    names = [ec.name for ec in edge_classes]
    a(f"  {' '.join(names)} : H3EdgeClass")
    a("")

    # Count per class
    a("h3EdgeCount : H3EdgeClass → ℕ")
    for ec in edge_classes:
        a(f"h3EdgeCount {ec.name} = {ec.count}")
    a("")

    # Valence per class
    a("h3Valence : H3EdgeClass → ℕ")
    for ec in edge_classes:
        a(f"h3Valence {ec.name} = {ec.valence_in_patch}")
    a("")

    # Curvature per class (in twentieths)
    a("-- Curvature in twentieths:  20 − 5·valence")
    a("h3Kappa : H3EdgeClass → ℚ₁₀")
    for ec in edge_classes:
        k20 = 20 - 5 * ec.valence_in_patch
        if k20 >= 0:
            a(f"h3Kappa {ec.name} = pos {k20}"
              f"    -- val={ec.valence_in_patch}, κ₂₀={k20}")
        else:
            a(f"h3Kappa {ec.name} = negsuc {-k20 - 1}"
              f"    -- val={ec.valence_in_patch}, κ₂₀={k20}")
    a("")

    # Total curvature sum
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  Total edge curvature (class-weighted sum over 12 edges")
    a("--  of the central cube)")
    a("-- ═══════════════════════════════════════════════════════════")
    a("")
    a("totalH3Curvature : ℚ₁₀")
    parts: list[str] = []
    for ec in edge_classes:
        parts.append(f"{ec.count} ·₁₀ h3Kappa {ec.name}")
    a(f"totalH3Curvature = {' +₁₀ '.join(parts)}")
    a("")

    # Compute expected sum
    total_val = sum(ec.count * (20 - 5 * ec.valence_in_patch)
                    for ec in edge_classes)
    if total_val >= 0:
        a(f"-- Expected value: pos {total_val}")
        a(f"totalH3Curvature-check : totalH3Curvature ≡ pos {total_val}")
    else:
        a(f"-- Expected value: negsuc {-total_val - 1}")
        a(f"totalH3Curvature-check : totalH3Curvature"
          f" ≡ negsuc {-total_val - 1}")
    a("totalH3Curvature-check = refl")
    a("")

    return "\n".join(L) + "\n"


def gen_obs(regions: list[RegionInfo]) -> str:
    """Generate Bridge/Honeycomb3DObs.agda."""
    L: list[str] = []
    a = L.append

    a("{-# OPTIONS --cubical --safe --guardedness #-}")
    a("")
    a("module Bridge.Honeycomb3DObs where")
    a("")
