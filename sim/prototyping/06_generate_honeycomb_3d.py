    a("open import Util.Scalars")
    a("open import Common.Honeycomb3DSpec")
    a("open import Common.ObsPackage")
    a("open import Boundary.Honeycomb3DCut")
    a("open import Bulk.Honeycomb3DChain")
    a("")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  Canonical views (dummy instances)")
    a("-- ═══════════════════════════════════════════════════════════")
    a("")
    a("h3BdyView : H3BdyView")
    a("h3BdyView .H3BdyView.dummy = 0")
    a("")
    a("h3BulkView : H3BulkView")
    a("h3BulkView .H3BulkView.dummy = 0")
    a("")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  Observable packages for the 3D {4,3,5} patch")
    a("-- ═══════════════════════════════════════════════════════════")
    a("")
    a("H3Obs∂ : ObsPackage H3Region")
    a("H3Obs∂ .ObsPackage.obs = S-cut h3BdyView")
    a("")
    a("H3ObsBulk : ObsPackage H3Region")
    a("H3ObsBulk .ObsPackage.obs = L-min h3BulkView")
    a("")

    # Pointwise agreement
    a("-- ═══════════════════════════════════════════════════════════")
    a(f"--  h3-pointwise — All {len(regions)} cases hold by refl")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--")
    a("--  Both S-cut and L-min return the same ℕ literal for each")
    a("--  region: both encode the true min-cut (min separating")
    a("--  surface area) through the 3D cell complex.")
    a("--")
    a("--  This is the 3D discrete Ryu–Takayanagi correspondence:")
    a("--  boundary entanglement entropy = bulk minimal surface area.")
    a("--")
    a(GENERATED_BANNER)
    a("")
    a("h3-pointwise :")
    a("  (r : H3Region) →")
    a("  S-cut h3BdyView r ≡ L-min h3BulkView r")

    for r in regions:
        rn = region_agda_name(r)
        a(f"h3-pointwise {rn} = refl")

    a("")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  h3-obs-path — Function equality via funExt")
    a("-- ═══════════════════════════════════════════════════════════")
    a("")
    a("h3-obs-path :")
    a("  S-cut h3BdyView ≡ L-min h3BulkView")
    a("h3-obs-path = funExt h3-pointwise")
    a("")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  h3-package-path — Package-level path")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--")
    a("--  This is the 3D analogue of filled-package-path: it")
    a("--  identifies the boundary and bulk observable packages")
    a("--  for the {4,3,5} honeycomb patch.  The downstream")
    a("--  enriched equivalence (Phase D.3) mirrors FilledEquiv.agda")
    a("--  using h3-obs-path in place of filled-obs-path.")
    a("--")
    a("")
    a("h3-package-path : H3Obs∂ ≡ H3ObsBulk")
    a("h3-package-path i .ObsPackage.obs = h3-obs-path i")
    a("")

    return "\n".join(L) + "\n"


