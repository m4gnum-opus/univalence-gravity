def region_agda_name(r: RegionInfo) -> str:
    """Agda constructor name for a region."""
    return f"h3r{r.idx}"


def cells_comment(r: RegionInfo) -> str:
    """Human-readable cell set for Agda comments."""
    return "{" + ",".join(f"c{c}" for c in sorted(r.cells)) + "}"


