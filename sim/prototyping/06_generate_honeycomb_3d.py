def gen_spec(regions: list[RegionInfo]) -> str:
    """Generate Common/Honeycomb3DSpec.agda."""
    L: list[str] = []
    a = L.append

    a("{-# OPTIONS --cubical --safe --guardedness #-}")
    a("")
    a("module Common.Honeycomb3DSpec where")
    a("")
    a("open import Cubical.Foundations.Prelude")
    a("open import Util.Scalars")
    a("")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  H3Region — Cell-aligned boundary regions of the")
    a("--  {4,3,5} 3D star patch (connected subsets of bdy cells)")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--")
    a("--  The patch is the BFS neighborhood of a central cube in")
    a("--  the {4,3,5} hyperbolic honeycomb (5 cubes per edge).")
    a("--  Boundary cells = cells with at least one exposed face.")
    a("--  Regions = connected subsets of boundary cells.")
    a("--")
    a(GENERATED_BANNER)
    a("")
    a("data H3Region : Type₀ where")

    # Group by region size
    sizes: dict[int, list[RegionInfo]] = defaultdict(list)
    for r in regions:
        sizes[len(r.cells)].append(r)

    for size in sorted(sizes.keys()):
        rs = sizes[size]
        names = [region_agda_name(r) for r in rs]
        a(f"  -- size {size}  ({len(rs)} regions)")
        # Emit in batches of 10 for readability
        for i in range(0, len(names), 10):
            batch = names[i:i+10]
            a(f"  {' '.join(batch)} : H3Region")

    a("")
    return "\n".join(L) + "\n"


