class EdgeClassInfo(NamedTuple):
    name: str
    valence_in_patch: int
    full_valence: int       # 5 in {4,3,5}
    count: int
    kappa_quarters: int     # curvature in units of π/2:  4 − valence


def compute_edge_classes(
    data: dict, patch: dict,
) -> list[EdgeClassInfo]:
    """
    Classify the 12 edges of the central cube by their valence
    within the patch.
    """
    face_crossings = data["face_crossings"]
    adj_pairs = data["adj_pairs"]
    cell_center = data["cell_center"]
    p = cell_center

    valences: list[int] = []
    for fi, fj in adj_pairs:
        F_a, F_b = face_crossings[fi], face_crossings[fj]
        dih_elems = proto.enumerate_group([F_a, F_b])
        in_patch = 0
        for g in dih_elems:
            c = g @ p
            k = proto._vec_key(c)
            for cid, mat in patch["cells"].items():
                if proto._vec_key(mat @ p) == k:
                    in_patch += 1
                    break
        valences.append(in_patch)

    # Group by valence
    val_counts = Counter(valences)
    classes: list[EdgeClassInfo] = []
    for val in sorted(val_counts.keys()):
        kappa_q = 4 - val  # in units of π/2
        name = f"ev{val}"
        classes.append(EdgeClassInfo(
            name=name,
            valence_in_patch=val,
            full_valence=5,
            count=val_counts[val],
            kappa_quarters=kappa_q,
        ))
    return classes


