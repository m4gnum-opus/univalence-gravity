MAX_CELLS = 7  # yields the "3D star" (~32 cells, 130 bdy faces)


def build_3d_patch() -> dict:
    """Build the {4,3,5} 3D star patch via Coxeter reflections."""
    G = proto.build_gram_matrix()
    refls = proto.build_reflections(G)
    proto.validate_reflections(G, refls)
    cell_center = proto.find_cell_center(G)
    stab = proto.enumerate_group(refls[:3], expected=48)
    face_crossings = proto.find_face_crossings(refls, stab, G)
    adj_pairs, _ = proto.classify_face_pairs(face_crossings)
    patch = proto.build_patch(
        face_crossings, cell_center, MAX_CELLS,
        name="{4,3,5} 3D Star Patch"
    )
    return {
        "patch": patch,
        "face_crossings": face_crossings,
        "adj_pairs": adj_pairs,
        "cell_center": cell_center,
        "gram": G,
    }


# ════════════════════════════════════════════════════════════════════
#  2.  Region Enumeration (connected subsets of boundary cells)
# ════════════════════════════════════════════════════════════════════

class RegionInfo(NamedTuple):
    idx: int
    cells: frozenset[int]
    n_bdy_faces: int
    min_cut: int


def identify_boundary_cells(patch: dict) -> dict[int, int]:
    """Return {cell_id: n_boundary_faces} for all boundary cells."""
    return {cid: n for cid, n in patch["bdy_per_cell"].items() if n > 0}


def build_boundary_adjacency(
    patch: dict, bdy_cells: set[int],
) -> nx.Graph:
    """Build adjacency graph on boundary cells (connected via internal faces)."""
    bg = nx.Graph()
    bg.add_nodes_from(bdy_cells)
    for c1, c2 in patch["internal_faces"]:
        if c1 in bdy_cells and c2 in bdy_cells:
            bg.add_edge(c1, c2)
    return bg


def enumerate_connected_subsets(
    graph: nx.Graph,
    max_size: int,
) -> list[frozenset[int]]:
    """Enumerate all connected subsets of nodes up to max_size."""
    results: set[frozenset[int]] = set()
    nodes = sorted(graph.nodes())

    for start in nodes:
        _dfs_subsets(graph, frozenset([start]), set(graph.neighbors(start)),
                     max_size, results)
    return sorted(results, key=lambda s: (len(s), tuple(sorted(s))))


def _dfs_subsets(
    graph: nx.Graph,
    current: frozenset[int],
    frontier: set[int],
    max_size: int,
    results: set[frozenset[int]],
) -> None:
    """DFS enumeration of connected subsets."""
    if current in results:
        return
    results.add(current)
    if len(current) >= max_size:
        return
    for node in sorted(frontier):
        new_set = current | frozenset([node])
        if new_set not in results:
            new_frontier = (
                frontier
                | set(graph.neighbors(node))
            ) - new_set
            _dfs_subsets(graph, new_set, new_frontier, max_size, results)


def compute_regions(
    patch: dict,
    max_region_cells: int = 4,
) -> list[RegionInfo]:
    """Enumerate boundary-cell regions and compute min-cuts."""
    bdy_cells_map = identify_boundary_cells(patch)
    bdy_cells = set(bdy_cells_map.keys())
    bg = build_boundary_adjacency(patch, bdy_cells)

    subsets = enumerate_connected_subsets(bg, max_region_cells)
    # Remove full boundary (complement is empty)
    if frozenset(bdy_cells) in subsets:
        subsets = [s for s in subsets if s != frozenset(bdy_cells)]

    # Build flow graph
    flow_G, bnode_ids, bnode_to_cell = proto.build_flow_graph(patch)

    # Map cell → boundary pseudo-nodes
    cell_bnodes: dict[int, set[int]] = defaultdict(set)
    for idx in bnode_ids:
        cell_bnodes[bnode_to_cell[idx]].add(idx)

    regions: list[RegionInfo] = []
    for i, subset in enumerate(subsets):
        idx_set: set[int] = set()
        for cid in subset:
            idx_set.update(cell_bnodes[cid])
        if not idx_set:
            continue
        s = proto.compute_min_cut(flow_G, bnode_ids, idx_set)
        regions.append(RegionInfo(i, subset, len(idx_set), s))

    # Re-index
    regions = [
        RegionInfo(i, r.cells, r.n_bdy_faces, r.min_cut)
        for i, r in enumerate(regions)
    ]
    return regions


# ════════════════════════════════════════════════════════════════════
#  3.  Edge Curvature Data
