def gen_curvature(edge_classes):
    L = []
    a = L.append
    a("{-# OPTIONS --cubical --safe --guardedness #-}")
    a(""); a("module Bulk.Dense1000Curvature where")
    a(""); a("open import Cubical.Foundations.Prelude")
    a("open import Cubical.Data.Nat  using (ℕ ; zero ; suc ; _+_ ; _·_)")
    a("open import Cubical.Data.Int")
    a("  using (ℤ ; pos ; negsuc)")
    a("  renaming (_+_ to _+ℤ_)")
    a(""); a("open import Util.Rationals")
    a(""); a(GENERATED_BANNER); a("")
    a("data D1000EdgeClass : Type₀ where")
    a(f"  {' '.join(ec.name for ec in edge_classes)} : D1000EdgeClass")
    a("")
    a("d1000EdgeCount : D1000EdgeClass → ℕ")
    for ec in edge_classes:
        a(f"d1000EdgeCount {ec.name} = {ec.count}")
    a("")
    a("d1000Kappa : D1000EdgeClass → ℚ₁₀")
    for ec in edge_classes:
        k20 = ec.kappa_twentieths
        if k20 >= 0:
            a(f"d1000Kappa {ec.name} = pos {k20}")
        else:
            a(f"d1000Kappa {ec.name} = negsuc {-k20 - 1}")
    a("")
    a("totalD1000Curvature : ℚ₁₀")
    parts = [f"{ec.count} ·₁₀ d1000Kappa {ec.name}" for ec in edge_classes]
    a(f"totalD1000Curvature = {' +₁₀ '.join(parts)}")
    a("")
    total_val = sum(ec.count * ec.kappa_twentieths for ec in edge_classes)
    exp = f"pos {total_val}" if total_val >= 0 else f"negsuc {-total_val - 1}"
    a(f"totalD1000Curvature-check : totalD1000Curvature ≡ {exp}")
    a("totalD1000Curvature-check = refl")
    a("")
    return "\n".join(L) + "\n"


