def gen_spec(regions, orbits):
    L = []
    a = L.append
    a("{-# OPTIONS --cubical --safe --guardedness #-}")
    a(""); a("module Common.Dense1000Spec where")
    a(""); a("open import Cubical.Foundations.Prelude")
    a("open import Util.Scalars"); a("")
    a(GENERATED_BANNER); a("")
    a("data D1000Region : Type₀ where")
    sizes: dict[int, list] = defaultdict(list)
    for r in regions:
        sizes[len(r.cells)].append(r)
    for size in sorted(sizes):
        rs = sizes[size]
        names = [region_agda_name(r) for r in rs]
        a(f"  -- size {size}  ({len(rs)} regions)")
        for i in range(0, len(names), 10):
            a(f"  {' '.join(names[i:i+10])} : D1000Region")
    a("")
    a("data D1000OrbitRep : Type₀ where")
    a(f"  {' '.join(o.name for o in orbits)} : D1000OrbitRep")
    a("")
    a(GENERATED_BANNER); a("")
    a("classify1000 : D1000Region → D1000OrbitRep")
    for r in regions:
        orb = orbit_for_region(r, orbits)
        a(f"classify1000 {region_agda_name(r)} = {orb.name}")
    a("")
    return "\n".join(L) + "\n"


