def gen_area_law(regions):
    L = []
    a = L.append
    a("{-# OPTIONS --cubical --safe --guardedness #-}")
    a(""); a("module Boundary.Dense1000AreaLaw where")
    a(""); a("open import Cubical.Foundations.Prelude")
    a("open import Cubical.Data.Nat using (ℕ ; _+_)")
    a("open import Util.Scalars")
    a("open import Common.Dense1000Spec")
    a("open import Boundary.Dense1000Cut")
    a("open import Bridge.Dense1000Obs using (d1000BdyView)")
    a(""); a(GENERATED_BANNER); a("")
    a("regionArea : D1000Region → ℚ≥0")
    for r in regions:
        a(f"regionArea {region_agda_name(r)} = {r.boundary_area}"
          f"    -- {len(r.cells)} cells")
    a("")
    a("abstract")
    a("  area-law : (r : D1000Region)"
      " → S-cut d1000BdyView r ≤ℚ regionArea r")
    prev_key = (-1, -1)
    for r in regions:
        key = (r.min_cut, r.slack)
        if key != prev_key:
            a(f"  -- S={r.min_cut}, area={r.boundary_area},"
              f" slack={r.slack}")
            prev_key = key
        a(f"  area-law {region_agda_name(r)} = {r.slack} , refl")
    a("")
    return "\n".join(L) + "\n"


def gen_half_bound(regions, achiever):
    L = []
    a = L.append
    a("{-# OPTIONS --cubical --safe --guardedness #-}")
    a(""); a("module Boundary.Dense1000HalfBound where")
    a(""); a("open import Cubical.Foundations.Prelude")
    a("open import Cubical.Data.Nat using (ℕ ; _+_)")
    a("open import Cubical.Data.Sigma using (Σ-syntax)")
    a(""); a("open import Util.Scalars")
    a("open import Common.Dense1000Spec")
    a("open import Boundary.Dense1000AreaLaw using (regionArea)")
    a("open import Bridge.GenericBridge using (PatchData ; orbit-to-patch)")
    a("open import Bridge.GenericValidation using (d1000OrbitPatch)")
    a("open import Bridge.HalfBound using (HalfBoundWitness)")
    a(""); a(GENERATED_BANNER); a("")
    a("private")
    a("  pd : PatchData")
    a("  pd = orbit-to-patch d1000OrbitPatch")
    a("")
    a("  S∂ : D1000Region → ℚ≥0")
    a("  S∂ = PatchData.S∂ pd")
    a("")
    a("abstract")
    a("  half-bound-proof : (r : D1000Region)"
      " → (S∂ r +ℚ S∂ r) ≤ℚ regionArea r")
    prev_key = (-1, -1)
    for r in regions:
        key = (r.min_cut, r.half_slack)
        if key != prev_key:
            a(f"  -- S={r.min_cut}, area={r.boundary_area},"
              f" 2·S={2*r.min_cut}, slack={r.half_slack}")
            prev_key = key
        a(f"  half-bound-proof {region_agda_name(r)} = {r.half_slack} , refl")
    a("")
    ach_rn = region_agda_name(achiever)
    a(f"tight-witness :"
      f" Σ[ r ∈ D1000Region ] (S∂ r +ℚ S∂ r ≡ regionArea r)")
    a(f"tight-witness = {ach_rn} , refl")
    a("")
    a("dense1000HalfBound : HalfBoundWitness pd")
    a("dense1000HalfBound .HalfBoundWitness.area = regionArea")
    a("dense1000HalfBound .HalfBoundWitness.half-bound = half-bound-proof")
    a("dense1000HalfBound .HalfBoundWitness.tight = tight-witness")
    a("")
    return "\n".join(L) + "\n"


