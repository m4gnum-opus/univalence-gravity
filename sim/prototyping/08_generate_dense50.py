    a("D50Obs∂ : ObsPackage D50Region")
    a("D50Obs∂ .ObsPackage.obs = S-cut d50BdyView")
    a("")
    a("D50ObsBulk : ObsPackage D50Region")
    a("D50ObsBulk .ObsPackage.obs = L-min d50BulkView")
    a("")

    # ── Pointwise agreement ─────────────────────────────────────────
    a("-- ═══════════════════════════════════════════════════════════")
    a(f"--  d50-pointwise — All {len(regions)} cases hold by refl")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--")
    a("--  Both S-cut and L-min return the same ℕ literal for each")
    a("--  region: both encode the true min-cut (min separating")
    a("--  surface area) through the 3D cell complex.")
    a("--")
    a("--  This is the 3D discrete Ryu–Takayanagi correspondence:")
    a("--  boundary entanglement entropy = bulk minimal surface area.")
    a("--")
    a("--  The Dense-50 patch achieves min-cut values from 2 to 7,")
    a("--  confirming genuine multi-face RT surfaces — unlike the")
    a("--  BFS shell patches where min-cut ≈ 1 for all singletons.")
    a("--")
    a(GENERATED_BANNER)
    a("")
    a("d50-pointwise :")
    a("  (r : D50Region) →")
    a("  S-cut d50BdyView r ≡ L-min d50BulkView r")

    for r in regions:
        rn = region_agda_name(r)
        a(f"d50-pointwise {rn} = refl")

    a("")

    # ── Function path ───────────────────────────────────────────────
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  d50-obs-path — Function equality via funExt")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--")
    a("--  Packages the 139 pointwise refl proofs into a single path")
    a("--  between the boundary and bulk observable functions:")
    a("--    S∂D50 ≡ LBD50")
    a("--")
    a("--  This is the 3D analogue of star-obs-path and")
    a("--  filled-obs-path, now on a densely-connected 3D patch")
    a("--  with non-trivial separating-surface geometry.")
    a("--")
    a("")
    a("S∂D50 : D50Region → ℚ≥0")
    a("S∂D50 = S-cut d50BdyView")
    a("")
    a("LBD50 : D50Region → ℚ≥0")
    a("LBD50 = L-min d50BulkView")
    a("")
    a("d50-obs-path : S∂D50 ≡ LBD50")
    a("d50-obs-path = funExt d50-pointwise")
    a("")

    # ── Package path ────────────────────────────────────────────────
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  d50-package-path — Package-level path")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--")
    a("--  Identifies the boundary and bulk observable packages.")
    a("--  The downstream enriched equivalence (Dense50Equiv.agda)")
    a("--  uses d50-obs-path to construct the Iso / Equiv / ua /")
    a("--  transport pipeline, mirroring Honeycomb3DEquiv.agda and")
    a("--  FilledEquiv.agda.")
    a("--")
    a("")
    a("d50-package-path : D50Obs∂ ≡ D50ObsBulk")
    a("d50-package-path i .ObsPackage.obs = d50-obs-path i")
    a("")

    return "\n".join(L) + "\n"


