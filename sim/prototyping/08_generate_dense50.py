def gen_spec(regions: list[RegionInfo]) -> str:
    """Generate  Common/Dense50Spec.agda ."""
    L: list[str] = []
    a = L.append

    a("{-# OPTIONS --cubical --safe --guardedness #-}")
    a("")
    a("module Common.Dense50Spec where")
    a("")
    a("open import Cubical.Foundations.Prelude")
    a("open import Util.Scalars")
    a("")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  D50Region — Cell-aligned boundary regions of the Dense-50")
    a("--  patch of the {4,3,5} hyperbolic honeycomb")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--")
    a("--  The Dense-50 patch is grown by greedy max-connectivity")
    a("--  from the central cube, producing 50 cells with 68 internal")
    a("--  shared faces and 164 boundary faces.  Regions are connected")
    a("--  subsets of boundary cells (up to 5 cells).")
    a("--")
    a("--  The dense growth strategy produces a multiply-connected")
    a("--  bulk, giving non-trivial min-cut values (2–7) that confirm")
    a("--  genuine multi-face RT separating surfaces in the 3D bulk.")
    a("--")
    a(GENERATED_BANNER)
    a("")
    a("data D50Region : Type₀ where")

    # Group by region size for readability
    sizes: dict[int, list[RegionInfo]] = defaultdict(list)
    for r in regions:
        sizes[len(r.cells)].append(r)

    for size in sorted(sizes.keys()):
        rs = sizes[size]
        names = [region_agda_name(r) for r in rs]
        a(f"  -- size {size}  ({len(rs)} regions)")
        for i in range(0, len(names), 10):
            batch = names[i:i + 10]
            a(f"  {' '.join(batch)} : D50Region")

    a("")
    return "\n".join(L) + "\n"


