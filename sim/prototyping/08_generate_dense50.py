def gen_curvature(edge_classes: list[EdgeClassInfo]) -> str:
    """Generate  Bulk/Dense50Curvature.agda ."""
    L: list[str] = []
    a = L.append

    a("{-# OPTIONS --cubical --safe --guardedness #-}")
    a("")
    a("module Bulk.Dense50Curvature where")
    a("")
    a("open import Cubical.Foundations.Prelude")
    a("open import Cubical.Data.Nat  using (ℕ ; zero ; suc ; _+_ ; _·_)")
    a("open import Cubical.Data.Int")
    a("  using (ℤ ; pos ; negsuc)")
    a("  renaming (_+_ to _+ℤ_)")
    a("")
    a("open import Util.Rationals")
    a("")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  3D Edge Curvature for the Dense-50 {4,3,5} Patch")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--")
    a("--  In 3D, curvature lives on EDGES (not vertices).")
    a("--  At an interior edge e with valence v (cubes meeting):")
    a("--    κ(e) = 2π − v · (π/2)")
    a("--")
    a("--  For {4,3,5}: full valence = 5, so κ = −π/2 (hyperbolic).")
    a("--  In the flat cubic tiling {4,3,4}: valence = 4, κ = 0.")
    a("--")
    a("--  Combinatorial encoding in twentieths (via ℚ₁₀ = ℤ):")
    a("--    κ₂₀(e) = 20 − 5·v")
    a("--      v=5 → κ₂₀ = −5   (hyperbolic)")
    a("--      v=4 → κ₂₀ =  0   (flat)")
    a("--      v=3 → κ₂₀ = +5   (boundary)")
    a("--      v=2 → κ₂₀ = +10  (boundary)")
    a("--")
    a("--  Edge valences are computed with deduplicated dihedral")
    a("--  orbit enumeration (07_honeycomb_3d_multiStrategy.py),")
    a("--  counting distinct cell centers in the patch.")
    a("--")
    a(GENERATED_BANNER)
    a("")

    # ── Edge class datatype ─────────────────────────────────────────
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  D50EdgeClass — Edge classes by in-patch valence")
    a("-- ═══════════════════════════════════════════════════════════")
    a("")
    a("data D50EdgeClass : Type₀ where")
    names = [ec.name for ec in edge_classes]
    a(f"  {' '.join(names)} : D50EdgeClass")
    a("")

    # ── Count per class ─────────────────────────────────────────────
    a("d50EdgeCount : D50EdgeClass → ℕ")
    for ec in edge_classes:
        a(f"d50EdgeCount {ec.name} = {ec.count}")
    a("")

    # ── Valence per class ───────────────────────────────────────────
    a("d50Valence : D50EdgeClass → ℕ")
    for ec in edge_classes:
        a(f"d50Valence {ec.name} = {ec.valence_in_patch}")
    a("")

    # ── Curvature per class (in twentieths) ─────────────────────────
    a("-- Curvature in twentieths:  κ₂₀ = 20 − 5·valence")
    a("d50Kappa : D50EdgeClass → ℚ₁₀")
    for ec in edge_classes:
        k20 = ec.kappa_twentieths
        if k20 >= 0:
            a(f"d50Kappa {ec.name} = pos {k20}"
              f"    -- val={ec.valence_in_patch}, κ₂₀={k20}")
        else:
            a(f"d50Kappa {ec.name} = negsuc {-k20 - 1}"
              f"    -- val={ec.valence_in_patch}, κ₂₀={k20}")
    a("")

    # ── Total curvature ─────────────────────────────────────────────
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  Total edge curvature (class-weighted sum over 12 edges")
    a("--  of the central cube)")
    a("-- ═══════════════════════════════════════════════════════════")
    a("")
    a("totalD50Curvature : ℚ₁₀")
    parts = [f"{ec.count} ·₁₀ d50Kappa {ec.name}" for ec in edge_classes]
    a(f"totalD50Curvature = {' +₁₀ '.join(parts)}")
    a("")

    # ── Gauss–Bonnet refl check ─────────────────────────────────────
    total_val = sum(ec.count * ec.kappa_twentieths for ec in edge_classes)
    if total_val >= 0:
        expected_str = f"pos {total_val}"
    else:
        expected_str = f"negsuc {-total_val - 1}"

    a(f"-- Expected value: {expected_str}  (= {total_val} in twentieths)")
    a(f"-- Interpretation: total edge curvature = {total_val}/20 × 2π")
    a(f"--")
    a(f"-- For the Dense-50 patch, all 12 central-cube edges are")
    a(f"-- fully surrounded by 5 cubes (the {{4,3,5}} condition),")
    a(f"-- confirming genuine 3D hyperbolic geometry.")
    a(f"")
    a(f"totalD50Curvature-check : totalD50Curvature ≡ {expected_str}")
    a("totalD50Curvature-check = refl")
    a("")

    # ── Per-class contribution checks ───────────────────────────────
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  Per-class contribution regression tests")
    a("-- ═══════════════════════════════════════════════════════════")
    a("")
    for ec in edge_classes:
        contrib = ec.count * ec.kappa_twentieths
        if contrib >= 0:
            contrib_str = f"pos {contrib}"
        else:
            contrib_str = f"negsuc {-contrib - 1}"
        a(f"-- {ec.count} edges × κ₂₀={ec.kappa_twentieths} = {contrib}")
        a(f"contrib-{ec.name} : {ec.count} ·₁₀ d50Kappa {ec.name}"
          f" ≡ {contrib_str}")
        a(f"contrib-{ec.name} = refl")
        a("")

    return "\n".join(L) + "\n"


def gen_obs(regions: list[RegionInfo]) -> str:
    """Generate  Bridge/Dense50Obs.agda ."""
    L: list[str] = []
    a = L.append

    a("{-# OPTIONS --cubical --safe --guardedness #-}")
    a("")
    a("module Bridge.Dense50Obs where")
    a("")
    a("open import Cubical.Foundations.Prelude")
    a("open import Util.Scalars")
    a("open import Common.Dense50Spec")
    a("open import Common.ObsPackage")
    a("open import Boundary.Dense50Cut")
    a("open import Bulk.Dense50Chain")
    a("")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  Canonical views (dummy instances)")
    a("-- ═══════════════════════════════════════════════════════════")
    a("")
    a("d50BdyView : D50BdyView")
    a("d50BdyView .D50BdyView.dummy = 0")
    a("")
    a("d50BulkView : D50BulkView")
    a("d50BulkView .D50BulkView.dummy = 0")
    a("")

    # ── Observable packages ─────────────────────────────────────────
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  Observable packages for the Dense-50 {4,3,5} patch")
    a("-- ═══════════════════════════════════════════════════════════")
