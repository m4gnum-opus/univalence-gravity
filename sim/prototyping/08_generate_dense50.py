# ════════════════════════════════════════════════════════════════════
#  1.  Build the Dense-50 Patch
# ════════════════════════════════════════════════════════════════════

def build_dense50_patch() -> dict:
    """
    Build the Dense-50 patch of the {4,3,5} honeycomb using the
    greedy max-connectivity growth strategy from 07.

    Returns a dict with the patch data plus Coxeter geometry objects
    needed for edge-class curvature computation.
    """
    G = multi.build_gram_matrix()
    refls = multi.build_reflections(G)
    multi.validate_reflections(G, refls)

    cell_center = multi.find_cell_center(G)
    stab = multi.enumerate_group(refls[:3], expected=48)
    face_crossings = multi.find_face_crossings(refls, stab, G)
    adj_pairs, opp_pairs = multi.classify_face_pairs(face_crossings)

    patch = multi.build_patch_dense(
        face_crossings, cell_center, MAX_CELLS,
        name="Dense-50 ({4,3,5} honeycomb)"
    )

    return {
        "patch": patch,
        "face_crossings": face_crossings,
        "adj_pairs": adj_pairs,
        "opp_pairs": opp_pairs,
        "cell_center": cell_center,
        "gram": G,
    }


# ════════════════════════════════════════════════════════════════════
#  2.  Region Computation
# ════════════════════════════════════════════════════════════════════

class RegionInfo(NamedTuple):
    idx: int
    cells: frozenset[int]
    n_bdy_faces: int
    min_cut: int


def compute_regions(patch: dict) -> list[RegionInfo]:
    """
    Enumerate all cell-aligned boundary regions (connected subsets
    of boundary cells up to MAX_REGION_CELLS cells) and compute
    their min-cut values via max-flow.
    """
    flow_G, bnode_ids, bnode_to_cell = multi.build_flow_graph(patch)

    # Use 07's region enumeration which returns (label, bnode_set) pairs
    regions_raw = multi.enumerate_cell_aligned_regions(
        patch, bnode_to_cell, bnode_ids,
        max_region_cells=MAX_REGION_CELLS,
    )

    regions: list[RegionInfo] = []
    for label, idx_set in regions_raw:
        # Parse cell IDs from label "{c0,c1,...}"
        cells_str = label.strip("{}").split(",")
        cells = frozenset(
            int(s.strip().lstrip("c")) for s in cells_str if s.strip()
        )
        s = multi.compute_min_cut(flow_G, bnode_ids, idx_set)
        regions.append(RegionInfo(len(regions), cells, len(idx_set), s))

    return regions


# ════════════════════════════════════════════════════════════════════
#  3.  Edge Curvature Classes
# ════════════════════════════════════════════════════════════════════

class EdgeClassInfo(NamedTuple):
    name: str
    valence_in_patch: int
    full_valence: int       # 5 in {4,3,5}
    count: int
    kappa_twentieths: int   # curvature encoding: 20 − 5·valence


def compute_edge_classes(data: dict, patch: dict) -> list[EdgeClassInfo]:
    """
    Classify the 12 edges of the central cube by their in-patch
    valence (number of dihedral-orbit cells actually present in
    the patch).

    Uses the deduplicated orbit-center approach from 07 (not the
    buggy double-counting in 05).
    """
    face_crossings = data["face_crossings"]
    adj_pairs = data["adj_pairs"]
    cell_center = data["cell_center"]
    p = cell_center

    patch_centers = {multi._vec_key(mat @ p)
                     for mat in patch["cells"].values()}

    valences: list[int] = []
    for fi, fj in adj_pairs:
        F_a, F_b = face_crossings[fi], face_crossings[fj]
        dih_elems = multi.enumerate_group([F_a, F_b])
        orbit_centers = {multi._vec_key(g @ p) for g in dih_elems}
        in_patch = len(orbit_centers & patch_centers)
        valences.append(in_patch)

    val_counts = Counter(valences)
    classes: list[EdgeClassInfo] = []
    for val in sorted(val_counts.keys()):
        k20 = 20 - 5 * val
        name = f"ev{val}"
        classes.append(EdgeClassInfo(
            name=name,
            valence_in_patch=val,
            full_valence=5,
            count=val_counts[val],
            kappa_twentieths=k20,
        ))
    return classes


# ════════════════════════════════════════════════════════════════════
#  4.  Agda Naming Helpers
