def print_summary(
    regions: list[RegionInfo],
    edge_classes: list[EdgeClassInfo],
    patch: dict,
) -> None:
    """Print a human-readable summary of the computed data."""
    print(f"\n  ── Patch Topology ──")
    print(f"  Cells:            {len(patch['cells'])}")
    print(f"  Internal faces:   {len(patch['internal_faces'])}")
    print(f"  Boundary faces:   {patch['n_boundary_faces']}")

    density = 2 * len(patch["internal_faces"]) / max(len(patch["cells"]), 1)
    print(f"  Density (2·IntF/Cells): {density:.2f}")

    cut_dist = Counter(r.min_cut for r in regions)
    size_dist = Counter(len(r.cells) for r in regions)

    print(f"\n  ── Region Statistics ({len(regions)} total) ──")
    print(f"  By size:")
    for sz in sorted(size_dist):
        print(f"    {sz}-cell regions:  {size_dist[sz]}")
    print(f"  By min-cut value:")
    for val in sorted(cut_dist):
        print(f"    S = {val}:  {cut_dist[val]} regions")

    min_s = min(r.min_cut for r in regions)
    max_s = max(r.min_cut for r in regions)
    print(f"  Min-cut range:  {min_s} – {max_s}")

    print(f"\n  ── Edge Curvature (12 central-cube edges) ──")
    for ec in edge_classes:
        k = ec.kappa_twentieths
        tag = " ← hyperbolic" if k < 0 else (" (flat)" if k == 0 else "")
        print(f"    Valence {ec.valence_in_patch} (×{ec.count}): "
              f"κ₂₀ = {k}{tag}")

    total_k = sum(ec.count * ec.kappa_twentieths for ec in edge_classes)
    print(f"  Total:  Σ κ₂₀ = {total_k}")

    n_full = sum(ec.count for ec in edge_classes
                 if ec.valence_in_patch >= 5)
    print(f"  Full-valence (≥5) edges: {n_full} of 12")


def print_gate_check(
    regions: list[RegionInfo],
    edge_classes: list[EdgeClassInfo],
    patch: dict,
) -> bool:
    """Print gate check and return True if all pass."""
    n_bdy = patch["n_boundary_faces"]
    n_regs = len(regions)
    n_full = sum(ec.count for ec in edge_classes
                 if ec.valence_in_patch >= 5)
    max_cut = max(r.min_cut for r in regions) if regions else 0

    print(f"\n  ═══════════════════════════════════════════════════════")
    print(f"  GATE CHECK (Dense-50 Feasibility)")
    print(f"  ═══════════════════════════════════════════════════════")

    gate_pass = True
    checks = [
        ("Boundary faces 100–500",
         100 <= n_bdy <= 500, n_bdy),
        ("Region count ≤ 500 (manageable for Agda)",
         n_regs <= 500, n_regs),
        ("≥1 full-valence edge in patch",
         n_full > 0, n_full),
        ("Max min-cut > 1 (genuine holographic depth)",
         max_cut > 1, max_cut),
    ]
    for desc, ok, val in checks:
        status = "✓ PASS" if ok else "✗ FAIL"
        if not ok:
            gate_pass = False
        print(f"    {status}  {desc}  (actual: {val})")

    if gate_pass:
        print(f"\n  ▶  ALL GATES PASS — Dense-50 Agda formalization FEASIBLE")
    else:
        print(f"\n  ▶  GATE(S) BLOCKED")

    return gate_pass


