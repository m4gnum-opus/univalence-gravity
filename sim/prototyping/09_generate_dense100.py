def gen_curvature(edge_classes: list[EdgeClassInfo]) -> str:
    """Generate  Bulk/Dense100Curvature.agda ."""
    L: list[str] = []
    a = L.append

    a("{-# OPTIONS --cubical --safe --guardedness #-}")
    a("")
    a("module Bulk.Dense100Curvature where")
    a("")
    a("open import Cubical.Foundations.Prelude")
    a("open import Cubical.Data.Nat  using (ℕ ; zero ; suc ; _+_ ; _·_)")
    a("open import Cubical.Data.Int")
    a("  using (ℤ ; pos ; negsuc)")
    a("  renaming (_+_ to _+ℤ_)")
    a("")
    a("open import Util.Rationals")
    a("")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  3D Edge Curvature for the Dense-100 {4,3,5} Patch")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--")
    a("--  In 3D, curvature lives on EDGES (not vertices).")
    a("--  At an interior edge e with valence v (cubes meeting):")
    a("--    κ(e) = 2π − v · (π/2)")
    a("--")
    a("--  For {4,3,5}: full valence = 5, so κ = −π/2 (hyperbolic).")
    a("--")
    a("--  Combinatorial encoding in twentieths (via ℚ₁₀ = ℤ):")
    a("--    κ₂₀(e) = 20 − 5·v")
    a("--      v=5 → κ₂₀ = −5   (hyperbolic)")
    a("--      v=4 → κ₂₀ =  0   (flat)")
    a("--      v=3 → κ₂₀ = +5   (boundary)")
    a("--      v=2 → κ₂₀ = +10  (boundary)")
    a("--")
    a(GENERATED_BANNER)
    a("")

    # Edge class datatype
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  D100EdgeClass — Edge classes by in-patch valence")
    a("-- ═══════════════════════════════════════════════════════════")
    a("")
    a("data D100EdgeClass : Type₀ where")
    names = [ec.name for ec in edge_classes]
    a(f"  {' '.join(names)} : D100EdgeClass")
    a("")

    a("d100EdgeCount : D100EdgeClass → ℕ")
    for ec in edge_classes:
        a(f"d100EdgeCount {ec.name} = {ec.count}")
    a("")

    a("d100Valence : D100EdgeClass → ℕ")
    for ec in edge_classes:
        a(f"d100Valence {ec.name} = {ec.valence_in_patch}")
    a("")

    a("-- Curvature in twentieths:  κ₂₀ = 20 − 5·valence")
    a("d100Kappa : D100EdgeClass → ℚ₁₀")
    for ec in edge_classes:
        k20 = ec.kappa_twentieths
        if k20 >= 0:
            a(f"d100Kappa {ec.name} = pos {k20}"
              f"    -- val={ec.valence_in_patch}, κ₂₀={k20}")
        else:
            a(f"d100Kappa {ec.name} = negsuc {-k20 - 1}"
              f"    -- val={ec.valence_in_patch}, κ₂₀={k20}")
    a("")

    # Total curvature
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  Total edge curvature (class-weighted sum over 12 edges")
    a("--  of the central cube)")
    a("-- ═══════════════════════════════════════════════════════════")
    a("")
    a("totalD100Curvature : ℚ₁₀")
    parts = [f"{ec.count} ·₁₀ d100Kappa {ec.name}" for ec in edge_classes]
    a(f"totalD100Curvature = {' +₁₀ '.join(parts)}")
    a("")

    total_val = sum(ec.count * ec.kappa_twentieths for ec in edge_classes)
    if total_val >= 0:
        expected_str = f"pos {total_val}"
    else:
        expected_str = f"negsuc {-total_val - 1}"

    a(f"-- Expected value: {expected_str}  (= {total_val} in twentieths)")
    a(f"")
    a(f"totalD100Curvature-check : totalD100Curvature ≡ {expected_str}")
    a("totalD100Curvature-check = refl")
    a("")

    # Per-class contributions
    a("-- ═════════════════════════════════════════════")
    a("--  Per-class contribution regression tests")
    a("-- ═════════════════════════════════════════════")
    a("")
    for ec in edge_classes:
        contrib = ec.count * ec.kappa_twentieths
        if contrib >= 0:
            contrib_str = f"pos {contrib}"
        else:
            contrib_str = f"negsuc {-contrib - 1}"
        a(f"-- {ec.count} edges × κ₂₀={ec.kappa_twentieths} = {contrib}")
        a(f"contrib-{ec.name} : {ec.count} ·₁₀ d100Kappa {ec.name}"
          f" ≡ {contrib_str}")
        a(f"contrib-{ec.name} = refl")
        a("")

    return "\n".join(L) + "\n"


