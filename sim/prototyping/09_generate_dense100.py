MAX_CELLS = 100
MAX_REGION_CELLS = 5


# ════════════════════════════════════════════════════════════════════
#  1.  Build the Dense-100 Patch
# ════════════════════════════════════════════════════════════════════

def build_dense100_patch() -> dict:
    """
    Build the Dense-100 patch of the {4,3,5} honeycomb using the
    greedy max-connectivity growth strategy from 07.

    Returns a dict with the patch data plus Coxeter geometry objects
    needed for edge-class curvature computation.
    """
    G = multi.build_gram_matrix()
    refls = multi.build_reflections(G)
    multi.validate_reflections(G, refls)

    cell_center = multi.find_cell_center(G)
    stab = multi.enumerate_group(refls[:3], expected=48)
    face_crossings = multi.find_face_crossings(refls, stab, G)
    adj_pairs, opp_pairs = multi.classify_face_pairs(face_crossings)

    patch = multi.build_patch_dense(
        face_crossings, cell_center, MAX_CELLS,
        name="Dense-100 ({4,3,5} honeycomb)"
    )

    return {
        "patch": patch,
        "face_crossings": face_crossings,
        "adj_pairs": adj_pairs,
        "opp_pairs": opp_pairs,
        "cell_center": cell_center,
        "gram": G,
    }


# ════════════════════════════════════════════════════════════════════
#  2.  Region Computation
# ════════════════════════════════════════════════════════════════════

class RegionInfo(NamedTuple):
    idx: int
    cells: frozenset[int]
    n_bdy_faces: int
    min_cut: int


def compute_regions(patch: dict) -> list[RegionInfo]:
    """
    Enumerate all cell-aligned boundary regions (connected subsets
    of boundary cells up to MAX_REGION_CELLS cells) and compute
    their min-cut values via max-flow.
    """
    flow_G, bnode_ids, bnode_to_cell = multi.build_flow_graph(patch)

    regions_raw = multi.enumerate_cell_aligned_regions(
        patch, bnode_to_cell, bnode_ids,
        max_region_cells=MAX_REGION_CELLS,
    )

    regions: list[RegionInfo] = []
    for label, idx_set in regions_raw:
        cells_str = label.strip("{}").split(",")
        cells = frozenset(
            int(s.strip().lstrip("c")) for s in cells_str if s.strip()
        )
        s = multi.compute_min_cut(flow_G, bnode_ids, idx_set)
        regions.append(RegionInfo(len(regions), cells, len(idx_set), s))

    return regions


# ════════════════════════════════════════════════════════════════════
#  3.  Orbit Reduction (by min-cut value)
# ════════════════════════════════════════════════════════════════════
#
#  The orbit reduction strategy from §6.5 of docs/10-frontier.md
#  classifies regions into orbits such that:
#    (a) all regions in an orbit have the same S-cut and L-min value
#    (b) proofs only case-split on orbit representatives
#
#  The simplest orbit classification that satisfies (a): group by
#  min-cut value.  This produces one orbit per distinct min-cut value
#  (typically ~8 orbits for the Dense-100 patch, versus ~717 regions).
#
#  This is a refinement-compatible choice: if a finer classification
#  is needed later (e.g. for subadditivity proofs), it can refine
#  these orbits without changing the bridge proofs.

class OrbitRep(NamedTuple):
    """An orbit representative, identified by its min-cut value."""
    name: str       # Agda constructor name: mc1, mc2, ...
    min_cut: int    # The shared min-cut value for all regions in this orbit
    count: int      # Number of regions in this orbit


def compute_orbits(regions: list[RegionInfo]) -> list[OrbitRep]:
    """Group regions by min-cut value into orbit representatives."""
    cut_counts = Counter(r.min_cut for r in regions)
    orbits: list[OrbitRep] = []
    for mc_val in sorted(cut_counts.keys()):
        orbits.append(OrbitRep(
            name=f"mc{mc_val}",
            min_cut=mc_val,
            count=cut_counts[mc_val],
        ))
    return orbits


def orbit_for_region(r: RegionInfo, orbits: list[OrbitRep]) -> OrbitRep:
    """Find the orbit representative for a region."""
    for orb in orbits:
        if orb.min_cut == r.min_cut:
            return orb
    raise ValueError(f"No orbit for min-cut {r.min_cut}")


# ════════════════════════════════════════════════════════════════════
#  4.  Edge Curvature Classes
# ════════════════════════════════════════════════════════════════════

class EdgeClassInfo(NamedTuple):
    name: str
    valence_in_patch: int
    full_valence: int
    count: int
    kappa_twentieths: int


def compute_edge_classes(data: dict, patch: dict) -> list[EdgeClassInfo]:
    """
    Classify the 12 edges of the central cube by their in-patch
    valence using the deduplicated orbit-center approach from 07.
    """
    face_crossings = data["face_crossings"]
    adj_pairs = data["adj_pairs"]
    cell_center = data["cell_center"]
    p = cell_center

    patch_centers = {multi._vec_key(mat @ p)
                     for mat in patch["cells"].values()}

    valences: list[int] = []
    for fi, fj in adj_pairs:
        F_a, F_b = face_crossings[fi], face_crossings[fj]
        dih_elems = multi.enumerate_group([F_a, F_b])
        orbit_centers = {multi._vec_key(g @ p) for g in dih_elems}
        in_patch = len(orbit_centers & patch_centers)
        valences.append(in_patch)

    val_counts = Counter(valences)
    classes: list[EdgeClassInfo] = []
    for val in sorted(val_counts.keys()):
        k20 = 20 - 5 * val
        classes.append(EdgeClassInfo(
            name=f"ev{val}",
            valence_in_patch=val,
            full_valence=5,
            count=val_counts[val],
            kappa_twentieths=k20,
        ))
    return classes


# ════════════════════════════════════════════════════════════════════
#  5.  Agda Naming Helpers
# ════════════════════════════════════════════════════════════════════

def region_agda_name(r: RegionInfo) -> str:
    return f"d100r{r.idx}"


def cells_comment(r: RegionInfo) -> str:
    return "{" + ",".join(f"c{c}" for c in sorted(r.cells)) + "}"


# ════════════════════════════════════════════════════════════════════
#  6.  Agda Module Generators
# ════════════════════════════════════════════════════════════════════

def gen_spec(
    regions: list[RegionInfo],
    orbits: list[OrbitRep],
) -> str:
    """Generate  Common/Dense100Spec.agda ."""
    L: list[str] = []
    a = L.append

    a("{-# OPTIONS --cubical --safe --guardedness #-}")
    a("")
    a("module Common.Dense100Spec where")
    a("")
    a("open import Cubical.Foundations.Prelude")
    a("open import Util.Scalars")
    a("")

    # ── D100Region ──────────────────────────────────────────────────
    a("-- ══════════════════════════════════════════════════════════════")
    a("--  D100Region — Cell-aligned boundary regions of the Dense-100")
    a("--  patch of the {4,3,5} hyperbolic honeycomb")
    a("-- ══════════════════════════════════════════════════════════════")
    a("--")
    a("--  The Dense-100 patch is grown by greedy max-connectivity")
    a(f"--  from the central cube, producing {MAX_CELLS} cells.")
    a(f"--  Regions are connected subsets of boundary cells (up to")
    a(f"--  {MAX_REGION_CELLS} cells), giving {len(regions)} regions.")
    a("--")
    a("--  This is the first use of the orbit reduction strategy in")
    a("--  the repository: the observable lookups are defined on the")
    a(f"--  small D100OrbitRep type ({len(orbits)} constructors), then")
    a(f"--  lifted to D100Region via classify100.")
    a("--")
    a(GENERATED_BANNER)
    a("")
    a("data D100Region : Type₀ where")

    sizes: dict[int, list[RegionInfo]] = defaultdict(list)
    for r in regions:
        sizes[len(r.cells)].append(r)

    for size in sorted(sizes.keys()):
        rs = sizes[size]
        names = [region_agda_name(r) for r in rs]
        a(f"  -- size {size}  ({len(rs)} regions)")
        for i in range(0, len(names), 10):
            batch = names[i:i + 10]
            a(f"  {' '.join(batch)} : D100Region")
    a("")

    # ── D100OrbitRep ────────────────────────────────────────────────
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  D100OrbitRep — Orbit representatives by min-cut value")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--")
    a("--  Each orbit representative corresponds to a distinct min-cut")
    a("--  value.  All regions in the same orbit have the same S-cut")
    a("--  and L-min value.  The observable functions are defined on")
    a("--  this small type, then lifted to D100Region via classify100.")
    a("--")
    a("--  Orbit decomposition:")
    for orb in orbits:
        a(f"--    {orb.name}  (S = {orb.min_cut}):  {orb.count} regions")
    a("--")
    a(GENERATED_BANNER)
    a("")
    a("data D100OrbitRep : Type₀ where")
    orb_names = [orb.name for orb in orbits]
    a(f"  {' '.join(orb_names)} : D100OrbitRep")
    a("")

    # ── classify100 ─────────────────────────────────────────────────
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  classify100 — Map each region to its orbit representative")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--")
    a(f"--  {len(regions)}-clause pattern match.  Each clause is")
    a("--  trivial (maps a constructor to a constructor).  This")
    a("--  function is traversed only during concrete evaluation,")
    a("--  never during proof normalization — the proofs operate on")
    a("--  D100OrbitRep and are lifted via this classification.")
    a("--")
    a(GENERATED_BANNER)
    a("")
    a("classify100 : D100Region → D100OrbitRep")

    for r in regions:
        rn = region_agda_name(r)
        orb = orbit_for_region(r, orbits)
        a(f"classify100 {rn} = {orb.name}")

    a("")
    return "\n".join(L) + "\n"


