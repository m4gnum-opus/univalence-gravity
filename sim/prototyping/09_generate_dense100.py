#!/usr/bin/env python3
"""
09_generate_dense100.py  —  Phase D.1c Dense-100 Agda Code Generator

Generates Cubical Agda modules for the Dense-100 patch of the {4,3,5}
hyperbolic honeycomb, utilizing the orbit reduction strategy from
§6.5 of docs/10-frontier.md to manage the ~717 cell-aligned boundary
regions.

Key innovation over Dense-50 (08_generate_dense50.py):
  The ~717 regions are classified into orbit representatives by their
  min-cut value (~8 orbits).  The observable lookup functions (S-cut,
  L-min) are defined on orbit representatives (~8 clauses), then
  lifted to full regions via a classification function.  This reduces
  the proof obligations from ~717 pointwise refl cases to ~8 orbit-
  representative refl cases, while the 717-clause classification
  function is traversed only during concrete evaluation — never
  during proof normalization.

  This is the first use of the orbit reduction strategy in the
  repository.  It demonstrates that the architecture scales past
  the ~500-constructor threshold where flat enumeration begins to
  stress Agda's parser, by factoring proofs through a small orbit
  type.

Generated modules:
  src/Common/Dense100Spec.agda        — D100Region (~717 ctors),
                                        D100OrbitRep (~8 ctors),
                                        classify100 (~717 clauses)
  src/Boundary/Dense100Cut.agda       — S-cut via orbit reps
  src/Bulk/Dense100Chain.agda         — L-min via orbit reps
  src/Bulk/Dense100Curvature.agda     — edge-class curvature + GB refl
  src/Bridge/Dense100Obs.agda         — orbit-based pointwise agreement

Usage (from repository root):
  python3 sim/prototyping/09_generate_dense100.py
  python3 sim/prototyping/09_generate_dense100.py --dry-run
  python3 sim/prototyping/09_generate_dense100.py --output-dir /path/to/src

Dependencies:  numpy, networkx

Reference:
  docs/10-frontier.md §6    (Direction D — 3D Hyperbolic Honeycombs)
  docs/10-frontier.md §6.5  (Stage 2 — Symmetry Quotient / Orbit Reduction)
  sim/prototyping/07_honeycomb_3d_multiStrategy.py  (Dense growth strategy)
  sim/prototyping/08_generate_dense50.py            (Dense-50 baseline)
"""

