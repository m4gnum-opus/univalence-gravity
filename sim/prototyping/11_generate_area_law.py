    print(f"  ✓  {len(regions)} regions computed.")

    if not regions:
        print("  ✗  No regions found!")
        return

    min_s = min(r.min_cut for r in regions)
    max_s = max(r.min_cut for r in regions)
    min_a = min(r.boundary_area for r in regions)
    max_a = max(r.boundary_area for r in regions)
    print(f"  Min-cut range:       {min_s} – {max_s}")
    print(f"  Boundary area range: {min_a} – {max_a}")

    # ── Step 3: Verify the area law ─────────────────────────────────
    print(f"\n  Verifying discrete area law: S(A) ≤ area(A) ...")
    ok = verify_area_law(regions)
    if ok:
        print(f"  ✓  Area law verified for all {len(regions)} regions!")
    else:
        print(f"  ✗  Area law VIOLATED — cannot generate Agda module.")
        return

    # ── Step 4: Print summary ───────────────────────────────────────
    print_summary(regions)
    print_sample(regions)

    # ── Step 5: Generate and write the Agda module ──────────────────
    src_dir = args.output_dir.resolve()
    out_path = src_dir / "Boundary" / "Dense100AreaLaw.agda"

    print(f"\n  Output: {out_path}")
