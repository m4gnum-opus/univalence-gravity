    print("╔═══════════════════════════════════════════════════════════╗")
    print("║  11: Discrete Area Law — Dense-100 {4,3,5} Honeycomb    ║")
    print("║  S_cut(A) ≤ area(A)  for all 717 boundary regions       ║")
    print("╚═══════════════════════════════════════════════════════════╝")

    # ── Step 1: Build the Dense-100 patch ───────────────────────────
    print("\n  Building Dense-100 patch via greedy max-connectivity ...")
    data = build_dense100_patch()
    patch = data["patch"]
    print(f"  ✓  {len(patch['cells'])} cells, "
          f"{len(patch['internal_faces'])} internal faces, "
          f"{patch['n_boundary_faces']} boundary faces.")

    # ── Step 2: Compute regions with boundary areas ─────────────────
    print(f"\n  Enumerating cell-aligned boundary regions "
          f"(max {MAX_REGION_CELLS} cells) ...")
    print("  Computing min-cut values and boundary surface areas ...")
