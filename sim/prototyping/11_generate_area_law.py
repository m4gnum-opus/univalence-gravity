# ════════════════════════════════════════════════════════════════════

def generate_area_law_module(regions: list[RegionAreaInfo]) -> str:
    """Generate  Boundary/Dense100AreaLaw.agda ."""
    L: list[str] = []
    a = L.append

    a("{-# OPTIONS --cubical --safe --guardedness #-}")
    a("")
    a("module Boundary.Dense100AreaLaw where")
    a("")
    a("open import Cubical.Foundations.Prelude")
    a("open import Cubical.Data.Nat using (ℕ ; _+_)")
    a("open import Util.Scalars")
    a("open import Common.Dense100Spec")
    a("open import Boundary.Dense100Cut")
    a("open import Bridge.Dense100Obs using (d100BdyView)")
    a("")

    # ── regionArea ──────────────────────────────────────────────────
    a("-- ═══════════════════════════════════════════════════════════")
    a("--  regionArea — Boundary surface area of each region")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--")
    a("--  For a cell-aligned boundary region A of k cells:")
    a("--")
    a("--    area(A) = 6k − 2 · |internal faces within A|")
    a("--")
    a("--  Each cube has 6 faces.  Each shared face between two")
    a("--  A-cells is internal to A and does not exit; we subtract 2")
    a("--  for each such face (once per cell sharing it).  The result")
    a("--  is the total count of faces connecting A to non-A entities")
    a("--  (neighboring cells outside A, or the patch exterior).")
    a("--")
    a("--  This is the discrete analogue of the Bekenstein–Hawking")
    a("--  area:  S ≤ A / 4G  becomes  S_cut(A) ≤ area(A)  with")
    a("--  the factor 1/4G absorbed into bond dimension and geometry.")
    a("--")
    a(GENERATED_BANNER)
    a("")
    a("regionArea : D100Region → ℚ≥0")

    for r in regions:
        rn = region_agda_name(r)
        tc = cells_comment(r)
        n = len(r.cells)
        a(f"regionArea {rn} = {r.boundary_area}"
          f"    -- {tc}  ({n} cells, area=6·{n}−2·"
          f"{(6*n - r.boundary_area)//2}={r.boundary_area})")

    a("")

    # ── area-law (abstract) ─────────────────────────────────────────
    a("-- ═══════════════════════════════════════════════════════════")
    a(f"--  area-law — S_cut(A) ≤ area(A)  ({len(regions)} cases)")
    a("-- ═══════════════════════════════════════════════════════════")
    a("--")
    a("--  The discrete isoperimetric inequality: the min-cut entropy")
    a("--  of any cell-aligned boundary region is bounded by its")
    a("--  boundary surface area within the cell complex.")
    a("--")
    a("--  This is a graph-theoretic tautology: the max-flow is")
    a("--  bounded by the capacity of any cut, and the trivial cut")
    a("--  (severing all faces on the boundary of A within the cell")
    a("--  complex) provides the upper bound.")
    a("--")
    a("--  Wrapped in  abstract  so that downstream modules never")
    a("--  re-unfold the 717-case proof.  Since  _≤ℚ_  is")
    a("--  propositional (by isProp≤ℚ from")
    a("--  Bridge/FullEnrichedStarObs.agda), the abstract barrier")
    a("--  does not damage any Univalence bridge.")
    a("--")
    a("--  Each witness  (k , refl)  type-checks because")
    a("--  k + S-cut d100BdyView r  reduces judgmentally to")
    a("--  regionArea r  via ℕ addition on closed numerals.")
    a("--")
    a("--  Reference:")
    a("--    docs/10-frontier.md §8.5  (the theorem statement)")
    a("--    docs/10-frontier.md §8.7  (Tier 2 implementation)")
    a("--    Approach A (abstract) + C (external oracle)")
    a("--")
    a(GENERATED_BANNER)
    a("")
    a("abstract")
    a("  area-law : (r : D100Region)"
      " → S-cut d100BdyView r ≤ℚ regionArea r")

    # Group by (min_cut, slack) for readability
    prev_key = (-1, -1)
    for r in regions:
        key = (r.min_cut, r.slack)
        if key != prev_key:
            a(f"  -- S={r.min_cut}, area={r.boundary_area},"
              f" slack={r.slack}:"
              f"  {r.slack} + {r.min_cut} = {r.boundary_area}")
            prev_key = key
        rn = region_agda_name(r)
        a(f"  area-law {rn} = {r.slack} , refl")

    a("")
    return "\n".join(L) + "\n"


# ════════════════════════════════════════════════════════════════════
#  5.  Verification and Summary
# ════════════════════════════════════════════════════════════════════

def verify_area_law(regions: list[RegionAreaInfo]) -> bool:
    """Verify that the area law holds for every region."""
    violations = []
    for r in regions:
        if r.slack < 0:
            violations.append(
                f"  VIOLATION at {region_agda_name(r)}: "
                f"S={r.min_cut} > area={r.boundary_area} "
                f"(slack={r.slack})"
            )
    if violations:
        for v in violations:
            print(v)
        return False
    return True


def print_summary(regions: list[RegionAreaInfo]) -> None:
    """Print a human-readable summary of the area-law data."""

    # Min-cut distribution
    cut_dist = Counter(r.min_cut for r in regions)
    area_dist = Counter(r.boundary_area for r in regions)
    slack_dist = Counter(r.slack for r in regions)
    size_dist = Counter(len(r.cells) for r in regions)

    print(f"\n  ── Region Statistics ({len(regions)} regions) ──")

    print(f"\n  By region size (number of cells):")
    for sz in sorted(size_dist):
        rs = [r for r in regions if len(r.cells) == sz]
        min_area = min(r.boundary_area for r in rs)
        max_area = max(r.boundary_area for r in rs)
        min_mc = min(r.min_cut for r in rs)
        max_mc = max(r.min_cut for r in rs)
        print(f"    {sz}-cell:  {size_dist[sz]:>4d} regions, "
              f"area {min_area}–{max_area}, "
              f"min-cut {min_mc}–{max_mc}")

    print(f"\n  Min-cut value distribution:")
    for val in sorted(cut_dist):
        print(f"    S = {val}:  {cut_dist[val]:>4d} regions")

    print(f"\n  Boundary area distribution:")
    for val in sorted(area_dist):
        print(f"    area = {val:>2d}:  {area_dist[val]:>4d} regions")

    print(f"\n  Slack distribution (area − min-cut):")
    for val in sorted(slack_dist):
        print(f"    k = {val:>2d}:  {slack_dist[val]:>4d} regions")

    min_slack = min(r.slack for r in regions)
    max_slack = max(r.slack for r in regions)
    avg_slack = sum(r.slack for r in regions) / len(regions)
    print(f"\n  Slack range:  {min_slack} – {max_slack}  "
          f"(mean {avg_slack:.1f})")

    # Area law tightness: how often does S = area?
    tight = sum(1 for r in regions if r.slack == 0)
    print(f"\n  Tight cases (S = area):  {tight} / {len(regions)}"
          f"  ({100*tight/len(regions):.1f}%)")


def print_sample(regions: list[RegionAreaInfo], n: int = 15) -> None:
    """Print a sample of regions with their area-law data."""
    print(f"\n  ── Sample Regions ──")
    print(f"  {'Region':<12s} | {'Cells':>5s} | {'S':>3s} | "
          f"{'Area':>4s} | {'Slack':>5s} | {'Witness':>12s}")
    print(f"  {'─' * 55}")

    # Sample across different sizes and slacks
    sample = sorted(regions, key=lambda r: (len(r.cells), r.min_cut))
    shown: set[tuple[int, int]] = set()
    count = 0
    for r in sample:
        key = (len(r.cells), r.min_cut)
        if key in shown:
            continue
        shown.add(key)
        rn = region_agda_name(r)
        print(f"  {rn:<12s} | {len(r.cells):>5d} | {r.min_cut:>3d} | "
              f"{r.boundary_area:>4d} | {r.slack:>5d} | "
              f"({r.slack} , refl)")
        count += 1
        if count >= n:
            break

    if len(regions) > n:
        print(f"  ... ({len(regions) - count} more)")


# ════════════════════════════════════════════════════════════════════
#  6.  File Writing
# ════════════════════════════════════════════════════════════════════

def write_file(path: Path, content: str, dry_run: bool) -> None:
    """Write content to path, creating parent directories as needed."""
    lines = content.count("\n")
    if dry_run:
        print(f"  [dry-run]  {path}  ({lines} lines)")
    else:
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content, encoding="utf-8")
        print(f"  ✓  {path}  ({lines} lines)")


