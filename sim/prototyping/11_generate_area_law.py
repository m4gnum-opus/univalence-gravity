#  1.  Build the Dense-100 Patch (reusing 07 infrastructure)
# ════════════════════════════════════════════════════════════════════

def build_dense100_patch() -> dict:
    """
    Build the Dense-100 patch of the {4,3,5} honeycomb using the
    greedy max-connectivity growth strategy from 07.

    Returns a dict with the patch data plus Coxeter geometry objects.
    """
    G = multi.build_gram_matrix()
    refls = multi.build_reflections(G)
    multi.validate_reflections(G, refls)

    cell_center = multi.find_cell_center(G)
    stab = multi.enumerate_group(refls[:3], expected=48)
    face_crossings = multi.find_face_crossings(refls, stab, G)
    adj_pairs, opp_pairs = multi.classify_face_pairs(face_crossings)

    patch = multi.build_patch_dense(
        face_crossings, cell_center, MAX_CELLS,
        name="Dense-100 ({4,3,5} honeycomb)"
    )

    return {
        "patch": patch,
        "face_crossings": face_crossings,
        "adj_pairs": adj_pairs,
        "cell_center": cell_center,
    }


# ════════════════════════════════════════════════════════════════════
#  2.  Region Computation (with boundary area)
# ════════════════════════════════════════════════════════════════════

class RegionAreaInfo(NamedTuple):
    idx: int
    cells: frozenset[int]
    n_bdy_faces: int         # number of boundary pseudo-nodes in region
    min_cut: int             # min-cut value (from max-flow)
    boundary_area: int       # 6|A| − 2·|internal faces within A|
    slack: int               # boundary_area − min_cut  (must be ≥ 0)


def compute_boundary_area(
    cells: frozenset[int],
    internal_faces: list[tuple[int, int]],
) -> int:
    """
    Boundary surface area of a cell-aligned region A.

        area(A) = 6·|A| − 2·|{shared faces between two cells in A}|

    This counts the total number of faces of A-cells that EXIT A
    (connecting to non-A cells or to the exterior of the patch).

    Each cube has 6 faces.  Each internal face shared between two
    A-cells is counted by both cells (contributing 2 to the 6|A|
    total) but does not exit A — so we subtract 2 per shared face.
    """
    n_cells = len(cells)
    internal_within = sum(
        1 for c1, c2 in internal_faces
        if c1 in cells and c2 in cells
    )
    return 6 * n_cells - 2 * internal_within


def compute_regions_with_area(patch: dict) -> list[RegionAreaInfo]:
    """
    Enumerate all cell-aligned boundary regions (connected subsets
    of boundary cells up to MAX_REGION_CELLS cells), compute their
    min-cut values and boundary surface areas.
    """
    flow_G, bnode_ids, bnode_to_cell = multi.build_flow_graph(patch)
    internal_faces = patch["internal_faces"]

    # Use 07's region enumeration
    regions_raw = multi.enumerate_cell_aligned_regions(
        patch, bnode_to_cell, bnode_ids,
        max_region_cells=MAX_REGION_CELLS,
    )

    regions: list[RegionAreaInfo] = []
    for label, idx_set in regions_raw:
        # Parse cell IDs from label "{c0,c1,...}"
        cells_str = label.strip("{}").split(",")
        cells = frozenset(
            int(s.strip().lstrip("c")) for s in cells_str if s.strip()
        )

        # Min-cut via max-flow
        s = multi.compute_min_cut(flow_G, bnode_ids, idx_set)

        # Boundary surface area
        area = compute_boundary_area(cells, internal_faces)

        # Slack (must be ≥ 0 for the area law to hold)
        slack = area - s

        regions.append(RegionAreaInfo(
            idx=len(regions),
            cells=cells,
            n_bdy_faces=len(idx_set),
            min_cut=s,
            boundary_area=area,
            slack=slack,
        ))

    return regions


# ════════════════════════════════════════════════════════════════════
#  3.  Agda Naming Helpers
# ════════════════════════════════════════════════════════════════════

def region_agda_name(r: RegionAreaInfo) -> str:
    """Agda constructor name for a region: d100r0, d100r1, ..."""
    return f"d100r{r.idx}"


def cells_comment(r: RegionAreaInfo) -> str:
    """Human-readable cell set for Agda comments."""
    return "{" + ",".join(f"c{c}" for c in sorted(r.cells)) + "}"


